  <h2 class="headline-dhamma"><ruby><rb>法</rb><rp>（</rp><rt>ダンマ</rt><rp>）</rp></ruby>の六徳</h2>
  <p class="ge-6toku-pali">Svākkhāto<span class="sup">1.</span> bhagavatā dhammo Sandiṭṭiko<span class="sup">2.</span> Akāliko<span class="sup">3.</span> Ehipassiko<span class="sup">4.</span> Opanaiko<span class="sup">5.</span> Paccattam veditabbo viññūhī<span class="sup">6.</span> ti.<br>
    Dhammaṃ jīvita pariyantaṃ saraṇaṃ gacchāmi.</p>
  <p class="ge-6toku-jp">世尊の法は</p>
  <ol class="ge-6toku-jp">
    <li>善く、正しく説き示された（教理、実践方法、論理、言語の上だけではなく修行の結果においても完全な）教えである。</li>
    <li>実証できる（いつでも誰にでも体験できる）教えである。</li>
    <li>普遍性があり、永遠たる（真理そのものであり、時と場合により訂正すべきものではない、即座に結果が得られる）教えである。</li>
    <li>「来れ見よ」（何人も試して、確かめてみよ）と言える確かな教えである。</li>
    <li>実践者を涅槃へ導く（煩悩に汚れたこころの状態を確実に浄化し、解脱へと導く）教えである。</li>
    <li> 賢者たちによって各自で悟られるべき（自らこの教えを実践して体験すべき、他力救済を説かない）教えである。</li>
  </ol>

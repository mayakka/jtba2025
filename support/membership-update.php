    <h2>登録情報の変更</h2>
    <ul>
      <li>会員情報として登録済みの住所、電話番号、メールアドレスを変更する場合は、以下のフォームに新しい情報を入力して送信してください。</li>
      <li>登録したクレジットカード情報を変更する場合は、「クレジットカード情報の変更を希望する」にチェックを入れて送信してください。後日、登録変更用のメールをお送りしますので、メールに記載されたURLから新しいクレジットカード情報を登録してください。</li>
      <li>アナログ登録会員でフォームの利用が難しい場合は、協会事務局宛にメール等でお知らせいただいても構いません。</li>
    </ul>

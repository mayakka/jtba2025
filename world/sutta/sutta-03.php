<section id="sect-1" class="sutta-box-single">
  <h1 class="headline-sutta-single">三宝に帰依するための偈文〔日夜の想い〕<span class="small-eng">Tisaraṇa Vandanā</span></h1>

  <ul class="sutta-order">
    <li class="sutta-numb digit1">1.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali">Namāmi Buddhaṃ guṇasāgarantaṃ,</dt>
        <dd class="raihai-yomi">ナマーミ ブッダン グナサーガランタン</dd>
        <dd class="raihai-wabun">大海の如き大徳者である、ブッダ（仏）に礼拝いたします。</p>〔尊き仏陀に帰依奉らん　その御徳は海の如くに果てしなし〕</dd>

        <dt class="raihai-pali">Sattā sadā hontu sukhī averā;</dt>
        <dd class="raihai-yomi">サッター サダー ホントゥ スキー アヴェーラー</dd>
        <dd class="raihai-wabun">生きとし生けるものが、いつも幸福・安穏に暮らせますように。</p>〔すべての衆生に幸あれと　安穏あれと願（ねご）うなり〕</dd>
        <dt class="raihai-pali">Kāyo jiguccho sakalo dugandho,</dt>
        <dd class="raihai-yomi">カーヨー ジグッチョー サカロー ドゥガンドー</dd>
        <dd class="raihai-wabun">この体は厭わしい、身の毛もよだつ悪臭のする汚物です。</p>〔この肉の身は不浄にて　骨の髄までおぞましき〕</dd>
        <dt class="raihai-pali">Gacchanti sabbe maraṇaṃ ahañ ca.</dt>
        <dd class="raihai-yomi">ガッチャンティ サッベー マラナン アハン チャ</dd>
        <dd class="raihai-wabun">すべての生命が死にゆくように、私も必ず死ぬべき存在です。</p>〔生命（しょう）あるものは崩れ果て　われも確かに死に至らん〕</dd>
      </dl>
    </li>
  </ul>

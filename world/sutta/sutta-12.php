  <h1 class="headline-sutta-single">「日々是好日」偈 <span class="small-eng">Bhaddekaratta gāthā</span></h1>

  <dl>
    <dt class="raihai-pali">Atītaṃ nānvāgameyya, nappaṭikaṅkhe anāgataṃ,
    </dt>
    <dd class="raihai-yomi">アティータン　ナーンワーガメイヤ　　ナッパティカンケー　アナーガタン</dd>
    <dd class="raihai-wabun">過去を追いゆくことなく　また未来を願いゆくことなし</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Yad atītaṃ pahīnaṃ taṃ, appattañ ca anāgataṃ.
    </dt>
    <dd class="raihai-yomi">ヤダティータン　パヒーナン　タン　　アッパッタン　チャ　アナーガタン</dd>
    <dd class="raihai-wabun">過去はすでに過ぎ去りしもの　未来は未だ来ぬものゆえに</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Paccuppannañ ca yo dhammaṃ, tattha tattha vipassati
    </dt>
    <dd class="raihai-yomi">パッチュッパンナン　チャ　ヨー　ダンマン　　タッタ　タッタ　ヴィパッサティ</dd>
    <dd class="raihai-wabun">現に存在している現象を　その場その場で観察し</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Asaṃhīraṃ asaṅkuppaṃ, taṃ vidvā manubrūhaye.
    </dt>
    <dd class="raihai-yomi">アサンヒーラン　アサンクッパン　　タン　ヴィドゥワー　マヌブルーハイェー</dd>
    <dd class="raihai-wabun">揺らぐことなく動じることなく　智者はそを修するがよい</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Ajjeva kiccaṃ ātappaṃ, ko jaññā maraṇaṃ suve
    </dt>
    <dd class="raihai-yomi">アッジェーワ キッチャン アータッパン　コー ジャンニャー マラナン スヴェー</dd>
    <dd class="raihai-wabun">今日こそ努め励むべきなり　誰が明日の死を知ろう</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Na hi no saṅgaraṃ tena, mahāsenena maccunā.
    </dt>
    <dd class="raihai-yomi">ナ　ヒ　ノー　サンガラン　テーナ　　マハーセーネーナ　マッチュナー</dd>
    <dd class="raihai-wabun">されば死の大軍に　我ら<ruby>
        <rb>煩</rb>
        <rp>（</rp>
        <rt>わずら</rt>
        <rp>）</rp>
      </ruby>うことなし</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Evaṃ vihāriṃ ātāpiṃ, ahorattam atanditaṃ
    </dt>
    <dd class="raihai-yomi">エーワン　ヴィハーリン　アーターピン　アホーラッタマタンディタン</dd>
    <dd class="raihai-wabun">昼夜怠ることなく　かように住み、励む</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Taṃ ve bhaddekarattoti, santo ācikkhate munīti.
    </dt>
    <dd class="raihai-yomi">タン ヴェー バッデーカラットーティ　サントー アーチッカテー ムニーティ</dd>
    <dd class="raihai-wabun">こはまさに「日々是好日」と　寂静者なる<ruby>
        <rb>牟尼</rb>
        <rp>（</rp>
        <rt>むに</rt>
        <rp>）</rp>
      </ruby>は説く</dd>
  </dl>


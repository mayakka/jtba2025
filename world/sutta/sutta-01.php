<section class="sutta-box-single">
  <h1 class="headline-sutta-single">諸仏の教え<span class="small-eng">(Dhammapada Nos. 183-185)</span></h1>

  <ul class="sutta-order">
    <li class="sutta-numb">183.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali">Sabba pāpassa akaraṇaṃ, kusalassa upasampadā, </dt>
        <dd class="raihai-yomi">サッバ パーパッサ アカラナン クサラッサ ウパサンパダー</dd>
        <dd class="raihai-wabun">一切の悪行為をおこなわないこと。善に至る（善行為をおこなう）こと。</dd>
        <dt class="raihai-pali">Sacitta pariyodapanaṃ, etaṃ Buddhāna sāsanaṃ.</dt>
        <dd class="raihai-yomi">サチッタ パリヨーダパナン エータン ブッダーナ サーサナン</dd>
        <dd class="raihai-wabun">自らの心を清めること。これが仏陀たちの教戒である。</dd>
      </dl>
    </li>
  </ul>
  <ul class="sutta-order">
    <li class="sutta-numb">184.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali">Khantī paramaṃ tapo titikkhā,</dt>
        <dd class="raihai-yomi">カンティー パラマン タポー ティティッカー</dd>
        <dd class="raihai-wabun">忍耐・堪忍は最上の修行である。</dd>
        <dt class="raihai-pali">Nibbānaṃ paramaṃ vadanti Buddhā;</dt>
        <dd class="raihai-yomi">ニッバーナン パラマン ワダンティ ブッダー</dd>
        <dd class="raihai-wabun">涅槃は最高のものであると、仏陀たちは説きたもう。</dd>
        <dt class="raihai-pali">Na hi pabbajito parūpaghāti,</dt>
        <dd class="raihai-yomi">ナ ヒ パッバジトー パルーパガーティ</dd>
        <dd class="raihai-wabun">他人を害する人は出家者ではない。</dd>
        <dt class="raihai-pali">Samaṇo hoti paraṃ viheṭhayanto.</dt>
        <dd class="raihai-yomi">サマノー ホーティ パラン ヴィヘータヤントー</dd>
        <dd class="raihai-wabun">他人を悩ます人は「道の人」ではない。</dd>
      </dl>
    </li>
  </ul>
  <ul class="sutta-order">
    <li class="sutta-numb">185.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali">Anūpavādo anūpaghāto Pātimokkhe ca saṃvaro,</dt>
        <dd class="raihai-yomi">アヌーパワードー アヌーパガートー パーティモッケー チャ サンワロー</dd>
        <dd class="raihai-wabun">罵らず、害わず、戒律に関しておのれを守り、</dd>
        <dt class="raihai-pali">Mattaññutā ca bhattasmiṃ pantañ ca sayanāsanaṃ,</dt>
        <dd class="raihai-yomi">マッタンニュター チャ バッタスミン パンタン チャ サヤナーサナン</dd>
        <dd class="raihai-wabun">食事に関して［適当な］量を知り、淋しいところでひとり臥し、坐し、</dd>
        <dt class="raihai-pali">Adhicitte ca āyogo etaṃ Buddhāna sāsanaṃ. </dt>
        <dd class="raihai-yomi">アディチッテー チャ アーヨーゴー エータン ブッダーナ サーサナン</dd>
        <dd class="raihai-wabun">心に関することにつとめはげむ。これが仏陀たちの教戒である。</dd>
      </dl>
    </li>
  </ul>

  <p class="matome">この三句は、仏教のエッセンスをまとめて釈迦尊が説かれたものです。人間の生きるべき道として普遍的なものであるが故に、すべての覚者が真理を語るならば以上の三句の内容を越えることはできません。この句の意味をよく理解し、毎日念じることにより我々の心の怒りや他を害する気持はなくなります。</p>


  <h1 class="headline-sutta-single">回向の文</h1>
  <h2 class="hl-kugiri">総合バージョン</h2>
  <p style="font-size: 18px; line-height: 2.5; margin-bottom: 2rem;">あらゆる生命に<ruby><rb>憐</rb><rp>（</rp><rt>あわ</rt><rp>）</rp></ruby>れみをもって真理を説かれた<br>
    <ruby><rb>本師釈迦牟尼佛陀</rb><rp>（</rp><rt>ほんししゃかむにぶっだ</rt><rp>）</rp></ruby>の聖なる教えに従い、<br>
    我々がここまで積んだすべての<ruby><rb>功徳</rb><rp>（</rp><rt>くどく</rt><rp>）</rp></ruby>を、<br>
    神々・先祖・祖父母・両親・親族・恩師をはじめとし、<br>
    一切の生きとし生けるものに<ruby><rb>回向</rb><rp>（</rp><rt>えこう</rt><rp>）</rp></ruby>いたします。<br>
    この功徳によって、<br>
    生きとし生けるものすべてが幸福に暮らせますように。<br>
    そして、<ruby><rb>解脱</rb><rp>（</rp><rt>げだつ</rt><rp>）</rp></ruby>を得られますように。</p>
  <h2 class="hl-kugiri">瞑想会バージョン</h2>
  <p style="font-size: 18px; line-height: 2.5; margin-bottom: 2rem;">佛法僧の三宝に礼拝、 帰依し、 戒をまもり、 慈悲の瞑想と、<br>
    ヴィパッサナー修行によって積まれたこの功徳を、<br>
    神々、 先祖、　祖父母、 両親、 親族、 恩師をはじめとし、<br>
    一切の生きとし生けるものに、回向いたします。<br>
    この功徳によって、生きとし生けるものすべてが幸福に暮らせますように。<br>
    そして、解脱を得られますように。</p>

<section id="sect-1" class="sutta-box-single">

  <h1 class="headline-sutta-single">吉祥経 <span class="small-eng">Mangala suttaṃ</span></h1>

  <dl>
    <dt class="raihai-pali">Evaṃ me sutaṃ: </dt>
    <dd class="raihai-yomi">エーワン　メー　スタン</dd>
    <dt class="raihai-pali">Ekaṃ samayaṃ Bhagavā Sāvatthiyaṃ viharati Jetavane </dt>
    <dd class="raihai-yomi">エーカン　サマヤン　バガワー　サーワッティヤン　ヴィハラティ　ジェータワネー</dd>
    <dt class="raihai-pali">Anāthapiṇḍikassa ārāme. </dt>
    <dd class="raihai-yomi">アナータピンディカッサ　アーラーメー</dd>
    <dd class="raihai-wabun u-mt-40">以下のように私は聞いております：
      ある時世尊は<ruby>
        <rb>舎衛城</rb>
        <rp>（</rp>
        <rt>しゃえいじょう</rt>
        <rp>）</rp>
      </ruby>の<ruby>
        <rb>祇樹給孤独園</rb>
        <rp>（</rp>
        <rt>ぎじゅきっこどくおん</rt>
        <rp>）</rp>
      </ruby>にお住まいでした。</dd>
  </dl>

  <dl class="u-mt-60">
    <dt class="raihai-pali">Atha kho aññatarā devatā abhikkantāya rattiyā abhikkantavaṇṇā </dt>
    <dd class="raihai-yomi">アタ　コー　アンニャタラー　デーワター　アビッカンターヤ　ラッティヤー　アビッカンタワンナー</dd>
    <dt class="raihai-pali">kevalakappaṃ Jetavanaṃ obhāsetvā yena Bhagavā ten' upasaṃkami, </dt>
    <dd class="raihai-yomi">ケーワラカッパン　ジェータワナン　オーバーセトゥワー　イェーナ　バガワー　テーヌパサンカミ</dd>
    <dt class="raihai-pali">upasaṃkamitvā Bhagavantaṃ abhivādetvā ekamantaṃ aṭṭhāsi. </dt>
    <dd class="raihai-yomi">ウパサンカミトゥワー　バガワンタン　アビワーデトゥワー　エーカマンタン　アッタースィ</dd>
    <dt class="raihai-pali">Ekamantaṃ ṭhitā kho sā devatā Bhagavantam gāthāya ajjhabhāsi: </dt>
    <dd class="raihai-yomi">エーカマンタン　ティター　コー　サー　デーワター　バガワンタン　ガーターヤ　アッジャバースィ</dd>
    <dd class="raihai-wabun u-mt-40">その時、一人の容色麗しい天人が夜半過ぎに、<ruby>
        <rb>祇園</rb>
        <rp>（</rp>
        <rt>ぎおん</rt>
        <rp>）</rp>
      </ruby>を<ruby>
        <rb>隈無</rb>
        <rp>（</rp>
        <rt>くまな</rt>
        <rp>）</rp>
      </ruby>く照らして世尊のおられる処に近付きました。
      近付いて、世尊に礼拝して、一方に立ちました。
      さて、一方に立ったその天人は、世尊に<ruby>
        <rb>詩偈</rb>
        <rp>（</rp>
        <rt>しげ</rt>
        <rp>）</rp>
      </ruby>によって申し上げました：
    </dd>
  </dl>

  <dl class="u-mt-60">
    <dt class="raihai-pali">“Bahū devā manussā ca maṅgalāni acintayuṃ </dt>
    <dd class="raihai-yomi">バフー　デーワー　マヌッサー　チャ　マンガラーニ　アチンタユン</dd>
    <dt class="raihai-pali">ākaṅkhamānā sotthānaṃ, brūhi maṅgalam uttamaṃ.” </dt>
    <dd class="raihai-yomi">アーカンカマーナー　ソッターナン　ブルーヒ　マンガラムッタマン</dd>
    <dd class="raihai-wabun u-mt-40">たくさんの天人や人々が、幸福を願って<ruby>
        <rb>吉祥</rb>
        <rp>（</rp>
        <rt>きちじょう</rt>
        <rp>）</rp>
      </ruby>なものごとを考えてきました。
      最上の吉祥をお説きください。</dd>
  </dl>

  <dl class="u-mt-60">
    <dt class="raihai-pali">“Asevanā ca bālānaṃ paṇḍitānañ ca sevanā </dt>
    <dd class="raihai-yomi">アセーワナー　チャ　バーラーナン　パンディターナン　チャ　セーワナー</dd>
    <dt class="raihai-pali">Pūjā ca pūjaneyyānaṃ, etaṃ maṅgalam uttamaṃ. </dt>
    <dd class="raihai-yomi">プージャー　チャ　プージャネイヤーナン　エータン　マンガラムッタマン</dd>
    <dd class="raihai-wabun u-mt-40">愚者たちに親近せず、賢者たちに親近すること、供養するに<ruby>
        <rb>相応</rb>
        <rp>（</rp>
        <rt>ふさわ</rt>
        <rp>）</rp>
      </ruby>しい人々を供養すること、これが最上の吉祥です。</dd>
  </dl>

  <dl class="u-mt-60">
    <dt class="raihai-pali">Paṭirūpa-desa-vāso ca pubbe ca katapuññatā </dt>
    <dd class="raihai-yomi">パティルーパデーサワーソー　チャ　プッベー　チャ　カタプンニャター</dd>
    <dt class="raihai-pali">Attasammāpaṇidhi ca, etaṃ maṅgalam uttamaṃ. </dt>
    <dd class="raihai-yomi">アッタサンマーパニディ　チャ　エータン　マンガラムッタマン</dd>
    <dd class="raihai-wabun u-mt-40">適切な処に住むこと、以前に為された<ruby>
        <rb>福業</rb>
        <rp>（</rp>
        <rt>ふくごう</rt>
        <rp>）</rp>
      </ruby>（善行為）<sup>＊1</sup>、自己について正しく志向すること、これが最上の吉祥です。<br><small>＊1 何かを行う前に必要な訓練などをして能力を整えること。</small></dd>
  </dl>

  <dl class="u-mt-60">
    <dt class="raihai-pali">Bāhusaccañ ca sippañ ca vinayo ca susikkhito </dt>
    <dd class="raihai-yomi">バーフサッチャン チャ　スィッパン チャ　ヴィナヨー チャ　ススィッキトー</dd>
    <dt class="raihai-pali">Subhāsitā ca yā vācā, etaṃ maṅgalam uttamaṃ. </dt>
    <dd class="raihai-yomi">スバースィター チャ　ヤー ワーチャー　エータン　マンガラムッタマン</dd>
    <dd class="raihai-wabun u-mt-40">たくさん聞き学ぶこと、技芸、善く身についている<ruby>
        <rb>躾</rb>
        <rp>（</rp>
        <rt>しつけ</rt>
        <rp>）</rp>
      </ruby>、言葉が善く語られること、これが最上の吉祥です。</dd>
  </dl>

  <dl class="u-mt-60">
    <dt class="raihai-pali">Mātāpitu-upaṭṭānaṃ puttadārassa saṅgaho </dt>
    <dd class="raihai-yomi">マーターピトゥ　ウパッターナン　プッタダーラッサ　サンガホー</dd>
    <dt class="raihai-pali">Anākulā ca kammantā, etaṃ maṅgalam uttamaṃ. </dt>
    <dd class="raihai-yomi">アナークラー　チャ　カンマンター　エータン　マンガラムッタマン</dd>
    <dd class="raihai-wabun u-mt-40">父母に孝行すること、妻子への責任を果たすこと、混乱のない仕事をすること、これが最上の吉祥です。</dd>
  </dl>

  <dl class="u-mt-60">
    <dt class="raihai-pali">Dānañ ca dhammacariyā ca ñātakānañ ca saṅgaho </dt>
    <dd class="raihai-yomi">ダーナン チャ　ダンマチャリヤー チャ　ニャータカーナン チャ　サンガホー</dd>
    <dt class="raihai-pali">Anavajjāni kammāni, etaṃ maṅgalam uttamaṃ. </dt>
    <dd class="raihai-yomi">アナワッジャーニ　カンマーニ　エータン　マンガラムッタマン</dd>
    <dd class="raihai-wabun u-mt-40">布施、教法に適う行い、親族への責任を果たすこと、<ruby>
        <rb>咎</rb>
        <rp>（</rp>
        <rt>とが</rt>
        <rp>）</rp>
      </ruby>のない行為、これが最上の吉祥です。</dd>
  </dl>

  <dl class="u-mt-60">
    <dt class="raihai-pali">Ārati virati pāpā majjapānā ca saññamo </dt>
    <dd class="raihai-yomi">アーラティ ヴィラティ パーパー　マッジャパーナー チャ サンニャモー</dd>
    <dt class="raihai-pali">Appamādo ca dhammesu, etaṃ maṅgalam uttamaṃ. </dt>
    <dd class="raihai-yomi">アッパマードー　チャ　ダンメース　エータン　マンガラムッタマン</dd>
    <dd class="raihai-wabun u-mt-40">悪法から離れ去ること、飲酒を自制すること、教法において怠けないこと、これが最上の吉祥です。</dd>
  </dl>

  <dl class="u-mt-60">
    <dt class="raihai-pali">Gāravo ca nivāto ca santuṭṭhī ca kataññutā </dt>
    <dd class="raihai-yomi">ガーラヴォー チャ　ニワートー チャ　サントゥッティー チャ　カタンニュター</dd>
    <dt class="raihai-pali">Kālena dhammasavanaṃ etaṃ maṅgalam uttamaṃ. </dt>
    <dd class="raihai-yomi">カーレーナ　ダンマサワナン　エータン　マンガラムッタマン</dd>
    <dd class="raihai-wabun u-mt-40">尊敬、謙譲、足るを知ること、恩を知ること、適時に教法を聞くこと、これが最上の吉祥です。</dd>
  </dl>

  <dl class="u-mt-60">
    <dt class="raihai-pali">Khantī ca sovacassatā samaṇānañ ca dassanaṃ </dt>
    <dd class="raihai-yomi">カンティー チャ　ソーワチャッサター　サマナーナン チャ　ダッサナン</dd>
    <dt class="raihai-pali">Kālena dhammasākacchā, etaṃ maṅgalam uttamaṃ. </dt>
    <dd class="raihai-yomi">カーレーナ　ダンマサーカッチャー　エータン　マンガラムッタマン</dd>
    <dd class="raihai-wabun u-mt-40">耐え忍ぶこと、素直に聞き入れること、沙門たちを見習うこと、適時に教法について話し合うこと、これが最上の吉祥です。</dd>
  </dl>

  <dl class="u-mt-60">
    <dt class="raihai-pali">Tapo ca brahmacariyañ ca ariyasaccāna dassanaṃ </dt>
    <dd class="raihai-yomi">タポー　チャ　ブラフマチャリヤン　チャ　アリヤサッチャーナ　ダッサナン</dd>
    <dt class="raihai-pali">Nibbānasacchikiriyā ca, etaṃ maṅgalam uttamaṃ. </dt>
    <dd class="raihai-yomi">ニッバーナサッチキリヤー　チャ　エータン　マンガラムッタマン</dd>
    <dd class="raihai-wabun u-mt-40">修行をすること、聖者に相応しい行為、［四］聖諦を観ること、涅槃をありありと覚ること、これが最上の吉祥です。</dd>
  </dl>

  <dl class="u-mt-60">
    <dt class="raihai-pali">Phuṭṭhassa lokadhammehi cittaṃ yassa na kampati </dt>
    <dd class="raihai-yomi">プッタッサ　ローカダンメーヒ　チッタン　ヤッサ　ナ　カンパティ</dd>
    <dt class="raihai-pali">Asokaṃ virajaṃ khemaṃ, etaṃ maṅgalam uttamaṃ. </dt>
    <dd class="raihai-yomi">アソーカン　ヴィラジャン　ケーマン　エータン　マンガラムッタマン</dd>
    <dd class="raihai-wabun u-mt-40">世間のものごとに触れても、心が動揺しないこと、憂いがなく、汚れがなく、安らかであること、これが最上の吉祥です。</dd>
  </dl>

  <dl class="u-mt-60">
    <dt class="raihai-pali">Etādisāni katvāna sabbattha-m-aparājitā </dt>
    <dd class="raihai-yomi">エーターディサーニ　カトゥワーナ　サッバッタマパラージター</dd>
    <dt class="raihai-pali">Sabbattha sotthiṃ gacchanti, taṃ tesaṃ maṅgalam uttamaṃ.” </dt>
    <dd class="raihai-yomi">サッバッタ　ソッティン　ガッチャンティ　タン　テーサン　マンガラムッタマン</dd>
    <dd class="raihai-wabun u-mt-40">以上のように行って、如何なる処でも打ち負かされず、あらゆる処で平安を得る、それが彼ら［人・天］にとっての最上の吉祥です。</dd>
  </dl>


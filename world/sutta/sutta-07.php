<section id="sect-1" class="sutta-box-single">
  <h1 class="headline-sutta-single">偉大なる人の思考 <span class="small-eng">Mahā purisa vitakka</span></h1>

  <ul class="sutta-order">
    <li class="sutta-numb">1.</li>
    <li class="sutta-cont">
    <dl>
      <dt class="raihai-pali">Appicchassāyaṃ dhammo, nāyaṃ dhammo mahicchassa;</dt>
      <dd class="raihai-yomi">アッピッチャッサーヤン ダンモー ナーヤン ダンモー マヒッチャッサ</dd>
      <dd class="raihai-wabun">この法（仏道）は少欲の人のものであり、多欲の人のものではない。</dd>
    </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">2.</li>
    <li class="sutta-cont">
    <dl>
      <dt class="raihai-pali">Santuṭṭhassāyaṃ dhammo, nāyaṃ dhammo asantuṭṭhassa;</dt>
      <dd class="raihai-yomi">サントゥッタッサーヤン ダンモー ナーヤン ダンモー アサントゥッタッサ</dd>
      <dd class="raihai-wabun">この法は足りることを知る人のものであり、不満な人のものではない。</dd>
    </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">3.</li>
    <li class="sutta-cont">
    <dl>
      <dt class="raihai-pali">Pavivittassāyaṃ dhammo, nāyaṃ dhammo saṅgaṇikārāmassa;</dt>
      <dd class="raihai-yomi">パヴィヴィッタッサーヤン ダンモー ナーヤン ダンモー サンガニカーラーマッサ</dd>
      <dd class="raihai-wabun">この法は遠離（独住）を喜ぶ人のものであり、衆会を楽しむ人のものではない。</dd>
    </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">4.</li>
    <li class="sutta-cont">
    <dl>
      <dt class="raihai-pali">Āraddhaviriyassāyaṃ dhammo, nāyaṃ dhammo kusītassa;</dt>
      <dd class="raihai-yomi">アーラッダヴィリヤッサーヤン ダンモー ナーヤン ダンモー クスィータッサ</dd>
      <dd class="raihai-wabun">この法は精進する人のものであり、怠け者のものではない。</dd>
    </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">5.</li>
    <li class="sutta-cont">
    <dl>
      <dt class="raihai-pali">Upaṭṭhitasatissāyaṃ dhammo, nāyaṃ dhammo muṭṭhassatissa;</dt>
      <dd class="raihai-yomi">ウパッティタサティッサーヤン ダンモー ナーヤン ダンモー ムッタッサティッサ</dd>
      <dd class="raihai-wabun">この法はよく気づく人のものであり、愚か者のものではない。</dd>
    </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">6.</li>
    <li class="sutta-cont">
    <dl>
      <dt class="raihai-pali">Samāhitassāyaṃ dhammo, nāyaṃ dhammo asamāhitassa;</dt>
      <dd class="raihai-yomi">サマーヒタッサーヤン ダンモー ナーヤン ダンモー アサマーヒタッサ</dd>
      <dd class="raihai-wabun">この法は心を統一した人のものであり、心を混乱している人のものではない。</dd>
    </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">7.</li>
    <li class="sutta-cont">
    <dl>
      <dt class="raihai-pali">Paññāvato ayaṃ dhammo, nāyaṃ dhammo duppaññassa;</dt>
      <dd class="raihai-yomi">パンニャーワトー アヤン ダンモー ナーヤン ダンモー ドゥッパンニャッサ</dd>
      <dd class="raihai-wabun">この法は智慧のある人のものであり、無知な人のものではない。</dd>
    </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">8.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali">Nippapañcārāmassāyaṃ dhammo nippapañcaratino;</dt>
        <dd class="raihai-yomi">ニッパパンチャーラーマッサーヤン ダンモー ニッパパンチャラティノー</dd>
        <dt class="raihai-pali">Nāyaṃ dhammo papañcārāmassa papañcaratino' ti.</dt>
        <dd class="raihai-yomi">ナーヤン ダンモー パパンチャーラーマッサ パパンチャラティノー ティ</dd>
        <dd class="raihai-wabun">この法は無為（現象を乗り越えた真理・涅槃）を喜ぶ、無為を楽しむ人のものであり、有為（現象、妄想の世界）を喜ぶ、有為を楽しむ人のものではない。</dd>
      </dl>
    </li>
  </ul>


  <h1 class="headline-sutta-single">朝昼お供え法要式文</h1>

  <h2 class="hl-kugiri">礼拝<span class="small-eng">Vandanā</span></h2>

  <dl>
    <dt class="raihai-pali">Namo Tassa Bhagavato Arahato Sammā Sambuddhassa.</dt>
    <dd class="raihai-yomi">ナモー　タッサ　バガワトー　アラハトー　サンマー　サンブッダッサ（三唱）</dd>
    <dd class="raihai-wabun">阿羅漢であり、正自覚者であり、福運に満ちた世尊に、私は敬礼いたします</dd>
  </dl>

  <h2 class="hl-kugiri">献灯<span class="small-eng">Āloka pūjā</span></h2>

  <dl>
    <dt class="raihai-pali">Ghanasārappa dittena dīpena tama dhaṃsinā,</dt>
    <dd class="raihai-yomi">ガナサーラッパ　ディッテーナ　ディーペーナ　タマ　ダンスィナー</dd>
    <dt class="raihai-pali">Tiloka dīpaṃ sambuddhaṃ pūjayāmi tamonudaṃ.</dt>
    <dd class="raihai-yomi">ティローカ　ディーパン　サンブッダン　プージャヤーミ　タモーヌダン</dd>
    <dd class="raihai-wabun">暗黒を破るこの灯りで、<br>
      三世界の暗闇を破る灯明である釈迦牟尼如来を礼し奉る。</dd>
  </dl>
  
  <h2 class="hl-kugiri">献花<span class="small-eng">Puppha pūjā</span></h2>

  <dl>
    <dt class="raihai-pali">Vaṇṇa gandha guṇopetaṃ etaṃ kusuma santatiṃ,</dt>
    <dd class="raihai-yomi">ワンナ　ガンダ　グノーペータン　エータン　クスマ　サンタティン</dd>
    <dt class="raihai-pali">Pūjayāmi munindassa sirīpāda saroruhe.</dt>
    <dd class="raihai-yomi">プージャヤーミ　ムニンダッサ　スィリーパーダ　サロールヘー</dd>
    <dd class="raihai-wabun" style="line-height: 2;">彩色・芳香により麗しき、あまたの<ruby><rb>御花</rb><rp>（</rp><rt>みはな</rt><rp>）</rp></ruby>で、<br>
      釈迦牟尼如来の<ruby><rb>御足</rb><rp>（</rp><rt>みあし</rt><rp>）</rp></ruby>を礼し奉る。</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Pūjemi buddhaṃ kusumena nena</dt>
    <dd class="raihai-yomi">プージェーミ　ブッダン　クスメーナ　ネーナ</dd>
    <dt class="raihai-pali">puññena metena labhāmi mokkhaṃ,</dt>
    <dd class="raihai-yomi">プンニェーナ　メーテーナ　ラバーミ　モッカン</dd>
    <dd class="raihai-wabun">我はこの花もて<ruby><rb>大聖</rb><rp>（</rp><rt>だいしょう</rt><rp>）</rp></ruby>佛陀に礼し奉らん。<br>
      この功徳によって解脱が得られんことを。</dd>
  </dl>
  <dl>

    <dd class="raihai-wabun"></dd>
  </dl>
  <dl>
    <dt class="raihai-pali">Pupphaṃ milāyāti yathā idam me</dt>
    <dd class="raihai-yomi">プッパン　ミラーヤーティ　ヤター　イダン　メー</dd>
    <dt class="raihai-pali">kāyo tathā yāti vināsabhāvaṃ.</dt>
    <dd class="raihai-yomi">カーヨー　タター　ヤーティ　ヴィナーサバーワン</dd>
    <dd class="raihai-wabun">必ず<ruby><rb>萎</rb><rp>（</rp><rt>しお</rt><rp>）</rp></ruby>れ枯れるこの<ruby><rb>供華</rb><rp>（</rp><rt>くうげ</rt><rp>）</rp></ruby>の如く、<br>
      我が肉体もまた滅び行くものなり。</dd>
  </dl>

  <h2 class="hl-kugiri">献香<span class="small-eng">Sugandha pūjā</span></h2>

  <dl>
    <dt class="raihai-pali">Sugandhikāya vadanaṃ ananta guṇa gandhinā</dt>
    <dd class="raihai-yomi">スガンディカーヤ　ワダナン　アナンタ　グナ　ガンディナー</dd>
    <dt class="raihai-pali">Sugandhinā haṃ gandhena pūjayāmi Tathāgataṃ.</dt>
    <dd class="raihai-yomi">スガンディナー　ハン　ガンデーナ　プージャヤーミ　タターガタン</dd>
    <dd class="raihai-wabun">その<ruby><rb>御言葉</rb><rp>（</rp><rt>みことば</rt><rp>）</rp></ruby>は香りの結晶の如き、無量の徳の香りをまといし、<br>
      釈迦牟尼如来をこの香もて礼し奉る。</dd>
  </dl>

  <h2 class="hl-kugiri">食事のお供え<span class="small-eng">Āhāra pūjā</span></h2>
 
  <p>飲み水：</p>
  <dl>
    <dt class="raihai-pali">Sugandhaṃ sītalaṃ kappaṃ pasanna madhuraṃ subhaṃ</dt>
    <dd class="raihai-yomi">スガンダン　スィータラン　カッパン　パサンナ　マドゥラン　スバン</dd>
    <dt class="raihai-pali">Pānīyam etaṃ bhagavā paṭigaṇhātu nāyako.</dt>
    <dd class="raihai-yomi">パーニーヤメータン　バガワー　パティガンハートゥ　ナーヤコー</dd>
    <dd class="raihai-wabun">透明にして、清らかなる、また、<ruby><rb>儀規</rb><rp>（</rp><rt>ぎき</rt><rp>）</rp></ruby>に<ruby><rb>適</rb><rp>（</rp><rt>かな</rt><rp>）</rp></ruby>いしこの飲み水を<br>
      釈迦牟尼如来に献上し奉る。<br>
      世尊がお受け取りをなされますように。</dd>
  </dl>

  <p>お食事：</p>
  <dl>
    <dt class="raihai-pali">Adhivāsetu no bhante bhojanaṃ parikappitaṃ</dt>
    <dd class="raihai-yomi">アディワーセートゥ　ノー　バンテー　ボージャナン　パリカッピタン</dd>
    <dt class="raihai-pali">Anukampaṃ upādāya paṭigaṇhātu muttamaṃ.</dt>
    <dd class="raihai-yomi">アヌカンパン　ウパーダーヤ　パティガンハートゥ　ムッタマン</dd>
    <dd class="raihai-wabun">偉大なる釈迦牟尼如来よ、我等が調理せしこの<ruby><rb>御食</rb><rp>（</rp><rt>おんじき</rt><rp>）</rp></ruby>を、<br>
      憐れみもて受けとられんことを願い奉る。</dd>
  </dl>
  <dl>
    <dt class="raihai-pali">Adhivāsetu no bhante sabbaṃ saddhāya pūjitaṃ</dt>
    <dd class="raihai-yomi">アディワーセートゥ　ノー　バンテー　サッバン　サッダーヤ　プージタン</dd>
    <dt class="raihai-pali">Anukampaṃ upādāya paṭigaṇhātu muttamaṃ.</dt>
    <dd class="raihai-yomi">アヌカンパン　ウパーダーヤ　パティガンハートゥ　ムッタマン</dd>
    <dd class="raihai-wabun">偉大なる釈迦牟尼如来よ、我等が用意せしその他の供物も、<br>
      憐れみもて受けとられんことを願い奉る。</dd>
  </dl>
  <dl>
    <dt class="raihai-pali">Nivedayāmi sambuddhaṃ vītarāgaṃ mahā muniṃ</dt>
    <dd class="raihai-yomi">ニヴェーダヤーミ　サンブッダン　ヴィータラーガン　マハー　ムニン</dd>
    <dt class="raihai-pali">Nimantayāmi sugataṃ lokajeṭṭhaṃ narāsabhaṃ.</dt>
    <dd class="raihai-yomi">ニマンタヤーミ　スガタン　ローカジェッタン　ナラーサバン</dd>
    <dd class="raihai-wabun" style="line-height: 2;">正覚者、煩悩の滅尽者、大聖者に食事の時をお告げし奉る。<br>
      <ruby><rb>善逝</rb><rp>（</rp><rt>ぜんぜい</rt><rp>）</rp></ruby>、<ruby><rb>世間上首</rb><rp>（</rp><rt>せけんじょうしゅ</rt><rp>）</rp></ruby>、<ruby><rb>至上人</rb><rp>（</rp><rt>しじょうにん</rt><rp>）</rp></ruby>を接待し奉る。</dd>
  </dl>

  <h2 class="hl-kugiri">三宝に対する懺悔<span class="small-eng">Khamāyācanā</span></h2>

  <dl>
    <dt class="raihai-pali">Kāyena vācā cittena pamādena mayā kataṃ Accayaṃ khama me bhante Bhūripañña Tathāgata.</dt>
    <dd class="raihai-yomi">カーイェーナ ワーチャー チッテーナ パマーデーナ マヤー カタン アッチャヤン カマ メー バンテー ブーリパンニャ タターガタ</dd>
    <dd class="raihai-wabun">大慧者たる如来に対し、<br>
      私が怠りによって犯した身口意の過ちを懺悔いたします。</dd>
    <dt class="raihai-pali">Kāyena vācā cittena pamādena mayā kataṃ Accayaṃ khama me Dhamma sandiṭṭhika akālika.</dt>
    <dd class="raihai-yomi">カーイェーナ ワーチャー チッテーナ パマーデーナ マヤー カタン アッチャヤン カマ メー ダンマ サンディッティカ アカーリカ</dd>
    <dd class="raihai-wabun">実証できる永遠の真理たる仏法に対し、<br>
      私が怠りによって犯した身口意の過ちを懺悔いたします。</dd>
    <dt class="raihai-pali">Kāyena vācā cittena pamādena mayā kataṃ Accayaṃ khama me Saṅgha supaṭipanna anuttara.</dt>
    <dd class="raihai-yomi">カーイェーナ ワーチャー チッテーナ パマーデーナ マヤー カタン アッチャヤン カマ メー サンガ スパティパンナ アヌッタラ</dd>
    <dd class="raihai-wabun">無上なる正道を実践する僧団に対し、<br>
      私が怠りによって犯した身口意の過ちを懺悔いたします。</dd>
  </dl>

  <h2 class="hl-kugiri">回向と誓願<span class="small-eng">Anumodanā</span></h2>
  <p>神々への回向：</p>
  <dl>
    <dt class="raihai-pali">Ettāvatā ca amhehi sambhataṃ puñña sampadaṃ</dt>
    <dd class="raihai-yomi">エッターワター　チャ　アンヘーヒ　サンバタン　プンニャ　サンパダン</dd>
    <dt class="raihai-pali">Sabbe devā anumodantu sabba sampatti siddhiyā.</dt>
    <dd class="raihai-yomi">サッベー デーワー　アヌモーダントゥ　サッバ　サンパッティ　スィッディヤー</dd>
    <dd class="raihai-wabun" style="line-height: 2;">ここまで我々が積みし全ての功徳なる財宝を、<br>
      神々が<ruby><rb>随喜</rb><rp>（</rp><rt>ずいき</rt><rp>）</rp></ruby> してお受け下さいますように。<br>
      <ruby><rb>遍</rb><rp>（</rp><rt>あまね</rt><rp>）</rp></ruby>く全ての神々が福徳に満たされますように。</dd>
  </dl>
  <dl>
    <dt class="raihai-pali">Ettāvatā ca amhehi sambhataṃ puñña sampadaṃ</dt>
    <dd class="raihai-yomi">エッターワター　チャ　アンヘーヒ　サンバタン　プンニャ　サンパダン</dd>
    <dt class="raihai-pali">Sabbe bhūtā anumodantu sabba sampatti siddhiyā.</dt>
    <dd class="raihai-yomi">サッベー ブーター アヌモーダントゥ サッバ サンパッティ スィッディヤー</dd>
    <dd class="raihai-wabun">ここまで我々が積みし全ての功徳なる財宝を、<br>
      精霊が随喜してお受け下さいますように。<br>
      遍く全ての精霊が福徳に満たされますように。</dd>
  </dl>
  <dl>
    <dt class="raihai-pali">Ettāvatā ca amhehi sambhataṃ puñña sampadaṃ　</dt>
    <dd class="raihai-yomi">エッターワター　チャ　アンヘーヒ　サンバタン　プンニャ　サンパダン</dd>
    <dt class="raihai-pali">Sabbe sattā anumodantu sabba sampatti siddhiyā.</dt>
    <dd class="raihai-yomi">サッベー サッター アヌモーダントゥ サッバ サンパッティ スィッディヤー</dd>
    <dd class="raihai-wabun">ここまで我々が積みし全ての功徳なる財宝を、<br>
      一切衆生が随喜してお受け下さいますように。<br>
      遍く一切の衆生が福徳に満たされますように。</dd>
  </dl>
  <p>先祖供養の偈：</p>
  <dl>
    <dt class="raihai-pali">Idaṃ me ñātīnaṃ hotu - sukhitā hontu ñātayo.</dt>
    <dd class="raihai-yomi">イダン メー ニャーティーナン ホートゥ　スキター ホントゥ ニャータヨー（三唱）</dd>
    <dd class="raihai-wabun">この功徳が先祖に届きますように。<br>
      先祖が安らぎに満たされますように。</dd>
  </dl>

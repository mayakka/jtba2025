  <h1 class="headline-sutta-single">食事の観察</h1>

  <p style="font-size: 18px; line-height: 2.5; margin-bottom: 2rem;"><ruby><rb>正覚者</rb><rp>（</rp><rt>しょうがくしゃ</rt><rp>）</rp></ruby>の説かれた真理を<ruby><rb>遵守</rb><rp>（</rp><rt>じゅんしゅ</rt><rp>）</rp></ruby>し、<br>
    正しく観察してこの食事をいただきます。</p>
  <p style="font-size: 18px; line-height: 2.5; margin-bottom: 2rem;">食事によりこころが汚れることを戒め、<br>
    身体を痛めることにも注意し、<br>
    壊れてゆくこの肉体の修復のために、<br>
    量を計って、この食事をいただきます。</p>
  <p style="font-size: 18px; line-height: 2.5; margin-bottom: 2rem;">一切の生命に対して慈しみの念を抱き、<br>
    釈尊の説かれた仏道を歩む目的を念頭において、<br>
    一切の現象は無常であることを随念しつつ、<br>
    この食事をいただきます。</p>
  <p style="color: #666;">＊食事をいただく前に、この文章をとなえてください。</p>


        <dd class="raihai-wabun">またその九つの孔からは、常に不浄物が流れ出る。目からは目やに、耳からは耳垢、</dd>
      </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">198.</li>
    <li class="sutta-cont">
      <dl>

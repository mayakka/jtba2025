  <p class="matome">勝利の経
    自分の身体をあまりにも可愛がると修行が後退することになります。身体を気にすることは執着です。執着を捨てるための実践を成功させるには「ありのままの身体を観察する」のです。お釈迦様の時代では修行者たちがこの経典を日夜唱えて、「身体のことが気になって修行に励めない」という心のわだかまりを解いたのです。解脱を体験しようとする戦いで勝利を得るための経典です。</p>


<section id="sect-1" class="sutta-box-single">
  <h1 class="headline-sutta-single">勝利の経 <span class="small-eng">Vijaya suttaṃ (Sutta nipāta I_11)</span></h1>

  <ul class="sutta-order">
    <li class="sutta-numb">193.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali">Caraṃ vā yadi vā tiṭṭhaṃ, nisinno uda vā sayaṃ;</dt>
        <dd class="raihai-yomi">チャラン ワー ヤディ ワー ティッタン ニスィンノー ウダ ワー サヤン</dd>
        <dt class="raihai-pali">Sammiñjeti pasāreti, esā kāyassa iñjanā.</dt>

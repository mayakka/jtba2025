<section id="sect-1" class="sutta-box-single">
  <h1 class="headline-sutta-single">因縁の教え <span class="small-eng">Paticca Samuppādo</span></h1>

  <h2 class="hl-kugiri">順観 <span class="small-eng">Anulomaṃ</span></h2>

  <dl>
    <dt class="raihai-pali">Avijjā<sup class="numb">①</sup> paccayā saṅkhārā<sup class="numb">②</sup>, saṅkhāra paccayā viññāṇaṃ<sup class="numb">③</sup>,</dt>
    <dd class="raihai-yomi">アヴィッジャー パッチャヤー サンカーラー　 サンカーラ パッチャヤー ヴィンニャーナン</dd>
    <dd class="raihai-wabun">①<span data-tooltip tabindex="1" title="無常等の真理に対する智慧がないこと">無明</span>に縁って②<span data-tooltip tabindex="2" title="現世において果報をもたらす心の波・業">行</span>が生じる。行に縁って③<span data-tooltip tabindex="3" title="生まれ（結生）の働きをする心">識</span>が生じる。</dd>
    <dt class="raihai-pali">viññāṇa paccayā nāmarūpaṃ<sup class="numb">④</sup>, nāmarūpa paccayā saḷāyatanaṃ<sup class="numb">⑤</sup>, </dt>
    <dd class="raihai-yomi">ヴィンニャーナ パッチャヤー ナーマルーパン　 ナーマルーパ パッチャヤー サラーヤタナン</dd>
    <dd class="raihai-wabun">識に縁って④<span data-tooltip tabindex="4" title="心の働きと体としての物質が生まれること">名色</span>が生じる。名色に縁って⑤<span data-tooltip tabindex="5" title="眼耳鼻舌身意という、外界の情報（対象）を受ける感覚器官">六処</span>が生じる。</dd>
    <dt class="raihai-pali">saḷāyatana paccayā phasso<sup class="numb">⑥</sup>, phassa paccayā vedanā<sup class="numb">⑦</sup>,</dt>
    <dd class="raihai-yomi">サラーヤタナ パッチャヤー パッソー　 パッサ パッチャヤー ヴェーダナー</dd>
    <dd class="raihai-wabun">六処に縁って⑥<span data-tooltip tabindex="6" title="外界の対象との接触">触</span>が生じる。触に縁って⑦<span data-tooltip tabindex="7" title="対象を感じること。すべての認識作用も“自我”意識も受によって生まれる">受</span>が生じる。</dd>
    <dt class="raihai-pali">vedanā paccayā taṇhā<sup class="numb">⑧</sup>, taṇhā paccayā upādānaṃ<sup class="numb">⑨</sup>,</dt>
    <dd class="raihai-yomi">ヴェーダナー パッチャヤー タンハー　 タンハー パッチャヤー ウパーダーナン</dd>
    <dd class="raihai-wabun">受に縁って⑧<span data-tooltip tabindex="8" title="欲が現れる。喉が渇いたようにまた対象に触れることを望む">渇愛</span>が生じる。渇愛に縁って⑨<span data-tooltip tabindex="9" title="主体としての自己にも、客体としての外界の対象にも強く囚われること">固執</span>が生じる。</dd>
    <dt class="raihai-pali">upādāna paccayā bhavo<sup class="numb">⑩</sup>, bhava paccayā jāti<sup class="numb">⑪</sup>,</dt>
    <dd class="raihai-yomi">ウパーダーナ パッチャヤー バヴォー　 バワ パッチャヤー ジャーティ</dd>
    <dd class="raihai-wabun">固執に縁って⑩<span data-tooltip tabindex="10" title="存在している生命の身、口、意の働きそのものが業（ポテンシャル）になること">有</span>が生じる。有に縁って⑪<span data-tooltip tabindex="11" title="新たに現象が現れる。再び生まれること">生</span>が生じる。</dd>
    <dt class="raihai-pali">jāti paccayā jarā maraṇaṃ soka parideva dukkha domanassa upāyāsā sambhavanti.</dt>
    <dd class="raihai-yomi">ジャーティ パッチャヤー ジャラー マラナン ソーカ パリデーワ ドゥッカ ドーマナッサ ウパーヤーサー サンバワンティ</dd>
    <dd class="raihai-wabun">生に縁って老、死 、憂愁、悲泣、苦しみ、悩み、落ち込みが現れる。</dd>
    <dt class="raihai-pali">Evam etassa kevalassa dukkhakkhandhassa samudayo hoti.</dt>
    <dd class="raihai-yomi">エーワ メータッサ ケーワラッサ ドゥッカッカンダッサ サムダヨー ホーティ</dd>
    <dd class="raihai-wabun">このようにして、このすべての苦蘊の生起がある。</dd>
  </dl>

  <h2 class="hl-kugiri">逆観 <span class="small-eng">Paṭilomaṃ</span></h2>

  <dl>
    <dt class="raihai-pali">Avijjā yatveva asesa virāga nirodhā saṅkhāra nirodho,</dt>
    <dd class="raihai-yomi">アヴィッジャー ヤットゥヴェーワ アセーサ ヴィラーガ ニローダー サンカーラ ニロードー</dd>
    <dd class="raihai-wabun">無明こそが、余すところなく離れて滅することに縁って行が滅する。</dd>
    <dt class="raihai-pali">saṅkhāra nirodhā viññāṇa nirodho, viññāṇa nirodhā nāma rūpa nirodho,</dt>
    <dd class="raihai-yomi">サンカーラ ニローダー ヴィンニャーナ ニロードー　 ヴィンニャーナ ニローダー ナーマ ルーパ ニロードー</dd>
    <dd class="raihai-wabun">行が滅することに縁って識が滅する。識が滅することに縁って名色が滅する。</dd>
    <dt class="raihai-pali">nāma rūpa nirodhā saḷāyatana nirodho, saḷāyatana nirodhā phassa nirodho,</dt>
    <dd class="raihai-yomi">ナーマ ルーパ ニローダー サラーヤタナ ニロードー　 サラーヤタナ ニローダー パッサ ニロードー</dd>
    <dd class="raihai-wabun">名色が滅することに縁って六処が滅する。六処が滅することに縁って触が滅する。</dd>
    <dt class="raihai-pali">phassa nirodhā vedanā nirodho, vedanā nirodhā taṇhā nirodho,</dt>
    <dd class="raihai-yomi">パッサ ニローダー ヴェーダナー ニロードー　 ヴェーダナー ニローダー タンハー ニロードー</dd>
    <dd class="raihai-wabun">触が滅することに縁って受が滅する。受が滅することに縁って渇愛が滅する。</dd>
    <dt class="raihai-pali">taṇhā nirodhā upādāna nirodho, upādāna nirodhā bhava nirodho,</dt>
    <dd class="raihai-yomi">タンハー ニローダー ウパーダーナ ニロードー　 ウパーダーナ ニローダー バワ ニロードー</dd>
    <dd class="raihai-wabun">渇愛が滅することに縁って固執が滅する。固執が滅することに縁って有が滅する。</dd>
    <dt class="raihai-pali">bhava nirodhā jāti nirodho,</dt>
    <dd class="raihai-yomi">バワ ニローダー ジャーティ ニロードー</dd>
    <dd class="raihai-wabun">有が滅することに縁って生が滅する。</dd>
    <dt class="raihai-pali">jāti nirodhā jarā maraṇaṃ soka parideva dukkha domanassa upāyāsā nirujjhanti.</dt>
    <dd class="raihai-yomi">ジャーティ ニローダー ジャラー マラナンソーカ パリデーワ ドゥッカ ドーマナッサ ウパーヤーサー ニルッジャンティ</dd>
    <dd class="raihai-wabun">生が滅することに縁って老、死、憂愁、悲泣、苦しみ、悩み、落ち込みが滅する。</dd>
    <dt class="raihai-pali">Evam etassa kevalassa dukkhakkhandhassa nirodho hoti .</dt>
    <dd class="raihai-yomi">エーワ メータッサ ケーワラッサ ドゥッカッカンダッサ ニロードー ホーティ</dd>
    <dd class="raihai-wabun">このようにして、このすべての苦蘊の滅がある。 </dd>
  </dl>

  <h2 class="hl-kugiri">歓喜の言葉 <span class="small-eng">Paṭhama udāna</span></h2>
  <dl>
    <dt class="raihai-pali">Aneka jāti saṃsāraṃ sandhāvissaṃ anibbisaṃ</dt>
    <dd class="raihai-yomi">アネーカ ジャーティ サンサーラン サンダーヴィッサン アニッビサン </dd>
    <dd class="raihai-wabun">無数の生涯にわたり、あてどなく輪廻をさまよってきた、</dd>
    <dt class="raihai-pali">Gahakārakaṃ gavesanto dukkhā jāti punappunaṃ.</dt>
    <dd class="raihai-yomi">ガハカーラカン ガヴェーサントー ドゥッカー ジャーティ プナップナン</dd>
    <dd class="raihai-wabun">家の作者を探し求めて。更に更にと、生まれ変わるのは苦しいことである。</dd>
    <dt class="raihai-pali">Gahakāraka, diṭṭho'si puna gehaṃ na kāhasi, </dt>
    <dd class="raihai-yomi">ガハカーラカ ディットースィ プナ ゲーハン ナ カーハスィ</dd>
    <dd class="raihai-wabun">家の作者（渇愛）よ、汝の正体は見られたり。汝が家屋を作ることはもはやあるまい。</dd>
    <dt class="raihai-pali">Sabbā te phāsukā bhaggā gahakūṭaṃ visaṅkhitaṃ</dt>
    <dd class="raihai-yomi">サッバー テー パースカー バッガー ガハクータン ヴィサンキタン</dd>
    <dd class="raihai-wabun">汝の梁（煩悩）はことごとく折れ、家の屋根（無明）は壊れてしまった。</dd>
    <dt class="raihai-pali">Visaṅkhāra gataṃ cittaṃ taṇhānaṃ khayam ajjhagā ti.</dt>
    <dd class="raihai-yomi">ヴィサンカーラ ガタン チッタン タンハーナン カヤマッジャガー ティ</dd>
    <dd class="raihai-wabun">形成するはたらき（行）から心は離れ、渇愛を滅ぼし尽くした。</dd>
  </dl>


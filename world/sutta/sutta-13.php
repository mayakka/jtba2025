  <h1 class="headline-sutta-single">祝福の偈 <span class="small-eng">Āsiṃsanā</span></h1>

  <dl>
    <dt class="raihai-pali">Sabbītiyo vivajjantu sabba rogo vinassatu</dt>
    <dd class="raihai-yomi">サッビーティヨー　ヴィワッジャントゥ　サッバ　ローゴー　ヴィナッサトゥ</dd>
    <dd class="raihai-wabun">一切の不幸・病苦を避けられますように。</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Mā no bhavatvantarāyo sukhī dīghāyuko bhava.</dt>
    <dd class="raihai-yomi">マー　ノー　バワトゥワンタラーヨー　スキー　ディーガーユコー　バワ</dd>
    <dd class="raihai-wabun">災難に遭うことなく、我らは末永く幸福に暮らせますように。</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Bhavatu sabba maṅgalaṃ rakkhantu sabba devata</dt>
    <dd class="raihai-yomi">バワトゥ　サッバ　マンガラン　ラッカントゥ　サッバ　デーワター</dd>
    <dd class="raihai-wabun">すべての祝福がありますように、諸天善神のご加護がありますように。</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Sabba buddhānubhāvena sadā sotthī bhavantu no.</dt>
    <dd class="raihai-yomi">サッバ　ブッダーヌバーヴェーナ　サダー　ソッティー　バワントゥ　ノー</dd>
    <dd class="raihai-wabun">佛（ブッダ）の威徳によって、我らは常に平安でありますように。</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Bhavatu sabba maṅgalaṃ rakkhantu sabba devata</dt>
    <dd class="raihai-yomi">バワトゥ　サッバ　マンガラン　ラッカントゥ　サッバ　デーワター</dd>
    <dd class="raihai-wabun">すべての祝福がありますように、諸天善神のご加護がありますように。</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Sabba dhammānubhāvena sadā sotthī bhavantu no.</dt>
    <dd class="raihai-yomi">サッバ　ダンマーヌバーヴェーナ　サダー　ソッティー　バワントゥ　ノー</dd>
    <dd class="raihai-wabun">法（ダンマ）の威徳によって、我らは常に平安でありますように。</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Bhavatu sabba maṅgalaṃ rakkhantu sabba devata</dt>
    <dd class="raihai-yomi">バワトゥ　サッバ　マンガラン　ラッカントゥ　サッバ　デーワター</dd>
    <dd class="raihai-wabun">すべての祝福がありますように、諸天善神のご加護がありますように。</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Sabba saṅghānubhāvena sadā sotthī bhavantu no.</dt>
    <dd class="raihai-yomi">サッバ　サンガーヌバーヴェーナ　サダー　ソッティー　バワントゥ　ノー</dd>
    <dd class="raihai-wabun">僧（サンガ）の威徳によって、我らは常に平安でありますように。</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Nakkhatta yakkha bhūtānaṃ pāpaggaha nivāraṇaṃ</dt>
    <dd class="raihai-yomi">ナッカッタ　ヤッカ　ブーターナン　パーパッガハ　ニワーラナン</dd>
    <dd class="raihai-wabun">星宿、悪霊や仇なす生命の影響から護られますように。</dd>
  </dl>

  <dl>
    <dt class="raihai-pali">Parittassānubhāvena hantu tesaṃ upaddave.</dt>
    <dd class="raihai-yomi">パリッタッサーヌバーヴェーナ　ハントゥ　テーサン　ウパッダヴェー</dd>
    <dd class="raihai-wabun">この護経（パリッタ）の威徳によって、我らは禍（わざわい）から解放されますように。</dd>
  </dl>


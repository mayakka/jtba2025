<section id="sect-1" class="sutta-box-single">
  <h1 class="headline-sutta-single">仏法僧（三宝）の徳の偈文</h1>

  <dl>
    <dt class="raihai-pali">Namo Tassa Bhagavato Arahato Sammā Sambuddhassa.</dt>
    <dd class="raihai-yomi">ナモー タッサ バガワトー アラハトー サンマー サンブッダッサ </dd>
    <dd class="raihai-wabun">私は阿羅漢であり、正自覚者であり、福運に満ちた世尊に敬礼したてまつる。</dd>
  </dl>

  <h2 class="hl-kugiri">仏陀の九徳 <span class="small-eng">Buddha Vandanā</span></h2>

  <dl>
    <dt class="raihai-pali">Iti pi so bhagavā Arahaṃ<sup class="numb">①</sup> Sammā sambuddho<sup class="numb">②</sup></dt>
    <dd class="raihai-yomi">イティ ピ ソー バガワー アラハン サンマー サンブッドー</dd>
    <dd class="raihai-wabun">世尊は ①<span data-tooltip tabindex="1" title="一切の煩悩を滅尽し、神々・人間の尊敬、供養を受けるに値する方">阿羅漢</span>であり、②<span data-tooltip tabindex="2" title="完全たる悟りを最初に覚って、その悟りへの道を他に教えられる方">正自覚者</span>であり、</dd>
    <dt class="raihai-pali">Vijjā-caraṇa sampanno<sup class="numb">③</sup> Sugato<sup class="numb">④</sup> Lokavidū<sup class="numb">⑤</sup></dt>
    <dd class="raihai-yomi">ヴィッジャーチャラナ サンパンノー スガトー ローカヴィドゥー</dd>
    <dd class="raihai-wabun">③<span data-tooltip tabindex="3" title="八種の智慧と十五種の行「性格に関する徳」が備わっている方〔みょうぎょうぐそくしゃ〕">明行具足者</span>であり、④<span data-tooltip tabindex="4" title="正しく涅槃に到達した、善く修行を完成した、正しく善い
      言葉を語る方〔ぜんぜい〕">善逝</span>であり、⑤<span data-tooltip tabindex="5" title="宇宙、衆生、諸行という三つの世界を知り尽くした方〔せけんげ〕">世間解</span>であり</dd>
    <dt class="raihai-pali">Anuttaro purisa damma sārathi<sup class="numb">⑥</sup></dt>
    <dd class="raihai-yomi">アヌッタロー プリサ ダンマ サーラティ</dd>
    <dd class="raihai-wabun">⑥<span data-tooltip tabindex="6" title="人々を指導することにおいては無上の能力を持つ方〔ちょうごじょうぶ〕">無上の調御丈夫</span>であり、</dd>
    <dt class="raihai-pali">Satthā deva manussānaṃ<sup class="numb">⑦</sup> Buddho<sup class="numb">⑧</sup> Bhagavā<sup class="numb">⑨</sup> ti.</dt>
    <dd class="raihai-yomi">サッター デーワ マヌッサーナン ブッドー バガワー ティ</dd>
    <dd class="raihai-wabun">⑦<span data-tooltip tabindex="7" title="人間、超次元的存在である神々等一切衆生の唯一の師〔てんにんし〕">天人師</span>であり、⑧<span data-tooltip tabindex="8" title="真理に目覚めた方、ブッダ">覚者</span>であり、⑨<span data-tooltip tabindex="9" title="すべての福徳を備えた方">世尊</span>であります。</dd>
    <dt class="raihai-pali">Buddhaṃ jīvita pariyantaṃ saraṇaṃ gacchāmi.</dt>
    <dd class="raihai-yomi">ブッダン ジーヴィタ パリヤンタン サラナン ガッチャーミ </dd>
    <dd class="raihai-wabun">［以上の徳が具われる］ブッダに、私は生涯帰依いたします。</dd>
  </dl>

  <h2 class="hl-kugiri">法の六徳 <span class="small-eng">Dhamma Vandanā</span></h2>

  <dl>
    <dt class="raihai-pali">Svākkhāto<sup class="numb">①</sup> bhagavatā dhammo</dt>
    <dd class="raihai-yomi">スワーッカートー バガワター ダンモー</dd>
    <dd class="raihai-wabun">世尊の法は ①<span data-tooltip tabindex="10" title="教理・実践方法・論理・言語の上だけでなく、修行の結果に於いても完全な教え">善く、正しく説き示された教え</span>です。</dd>
    <dt class="raihai-pali">Sandiṭṭhiko<sup class="numb">②</sup> Akāliko<sup class="numb">③</sup></dt>
    <dd class="raihai-yomi">サンディッティコー アカーリコー</dd>
    <dd class="raihai-wabun">②<span data-tooltip tabindex="11" title="いつでも誰にでも体験できる教え">実証できる教え</span>です。③<span data-tooltip tabindex="12" title="真理そのものであり、時と場合により訂正すべきものではない、即座に結果が得られる">普遍性があり、永遠たる教え</span>です。</dd>
    <dt class="raihai-pali">Ehipassiko<sup class="numb">④</sup> Opanayiko<sup class="numb">⑤</sup></dt>
    <dd class="raihai-yomi">エーヒパッスィコー オーパナイコー</dd>
    <dd class="raihai-wabun">④<span data-tooltip tabindex="13" title="何人も試して、確かめてみよ、と言える教え">「来たれ見よ」と言える確かな教え</span>です。⑤<span data-tooltip tabindex="14" title="煩悩に汚れた心の状態を確実に浄化し解脱へと導く教え">実践者を涅槃へ導く教え</span>です。</dd>
    <dt class="raihai-pali">Paccattaṃ veditabbo viññūhī<sup class="numb">⑥</sup> ti.</dt>
    <dd class="raihai-yomi">パッチャッタン ヴェーディタッボー ヴィンニューヒー ティ</dd>
    <dd class="raihai-wabun">⑥<span data-tooltip tabindex="15" title="自らこの教えを実践して体験すべき、他力救済を説かない教え">賢者たちによって各自で悟られるべき教え</span>です。</dd>
    <dt class="raihai-pali">Dhammaṃ jīvita pariyantaṃ saraṇaṃ gacchāmi.</dt>
    <dd class="raihai-yomi">ダンマン ジーヴィタ パリヤンタン サラナン ガッチャーミ</dd>
    <dd class="raihai-wabun">［以上の徳が具われる］ダンマに、私は生涯帰依します。 </dd>
  </dl>

  <h2 class="hl-kugiri">僧伽の九徳 <span class="small-eng">Saṅgha Vandanā</span></h2>

  <dl>
    <dt class="raihai-pali">Supaṭipanno bhagavato sāvakasaṅgho<sup class="numb">①</sup></dt>
    <dd class="raihai-yomi">スパティパンノー バガワトー サーワカサンゴー </dd>
    <dd class="raihai-wabun">①世尊の弟子たる僧（僧団）は正しい道を実践するものであり、</dd>
    <dt class="raihai-pali">Ujupaṭipanno bhagavato sāvakasaṅgho<sup class="numb">②</sup></dt>
    <dd class="raihai-yomi">ウジュパティパンノー バガワトー サーワカサンゴー</dd>
    <dd class="raihai-wabun">②真直ぐの道（涅槃への直道）を歩むものであり、</dd>
    <dt class="raihai-pali">Ñāyapaṭipanno bhagavato sāvakasaṅgho<sup class="numb">③</sup></dt>
    <dd class="raihai-yomi">ニャーヤパティパンノー バガワトー サーワカサンゴー</dd>
    <dd class="raihai-wabun">③涅槃を目指して修行するものであり、</dd>
    <dt class="raihai-pali">Sāmīcipaṭipanno bhagavato sāvakasaṅgho<sup class="numb">④</sup></dt>
    <dd class="raihai-yomi">サーミーチパティパンノー バガワトー サーワカサンゴー</dd>
    <dd class="raihai-wabun">④尊敬に値する道を実践するものであります。</dd>
    <dt class="raihai-pali">Yadidaṃ cattāri purisayugāni aṭṭhapurisa puggalā</dt>
    <dd class="raihai-yomi">ヤディダン チャッターリ プリサユガーニ アッタプリサ プッガラー</dd>
    <dd class="raihai-wabun">これらは四双八輩という八類に属する（聖者の位を得た）世尊の弟子達を指す。</dd>
    <dt class="raihai-pali">Esa bhagavato sāvakasaṅgho</dt>
    <dd class="raihai-yomi">エーサ バガワトー サーワカサンゴー</dd>
    <dd class="raihai-wabun">この世尊の弟子（声聞）たる僧団は、</dd>
    <dt class="raihai-pali">Āhuneyyo<sup class="numb">⑤</sup> Pāhuneyyo<sup class="numb">⑥</sup> Dakkhiṇeyyo<sup class="numb">⑦</sup> Añjali karaṇīyo<sup class="numb">⑧</sup></dt>
    <dd class="raihai-yomi">アーフネイヨー パーフネイヨー ダッキネイヨー アンジャリ カラニーヨー</dd>
    <dd class="raihai-wabun">⑤遠くから持ってくるものを受けるに値し、⑥来客として接待を受けるに値し、⑦徳を積むために供えるものを受けるに値し、⑧礼拝を受けるに値する、</dd>
    <dt class="raihai-pali">Anuttaraṃ puññakkhettaṃ lokassā ti.<sup class="numb">⑨</sup></dt>
    <dd class="raihai-yomi">アヌッタラン プンニャッケッタン ローカッサー ティ</dd>
    <dd class="raihai-wabun">⑨世の無上の福田です。</dd>
    <dt class="raihai-pali">Saṅghaṃ jīvita pariyantaṃ saraṇaṃ gacchāmi.</dt>
    <dd class="raihai-yomi">サンガン ジーヴィタ パリヤンタン サラナン ガッチャーミ</dd>
    <dd class="raihai-wabun">［以上の徳が具われる］サンガに、私は生涯帰依いたします。</dd>
  </dl>

  <h2 class="hl-kugiri">三宝に対する懺悔 <span class="small-eng">Khamāyācanā</span></h2>

  <dl>
    <dt class="raihai-pali">Kāyena vācā cittena pamādena mayā kataṃ Accayaṃ khama me bhante Bhūripañña Tathāgata.</dt>
    <dd class="raihai-yomi">カーイェーナ ワーチャー チッテーナ パマーデーナ マヤー カタン アッチャヤン カマ メー バンテー ブーリパンニャ タターガタ</dd>
    <dd class="raihai-wabun">大慧者たる如来に対し、私が怠りによって犯した身口意の過ちを懺悔いたします。</dd>
    <dt class="raihai-pali">Kāyena vācā cittena pamādena mayā kataṃ Accayaṃ khama me Dhamma sandiṭṭhika akālika.</dt>
    <dd class="raihai-yomi">カーイェーナ ワーチャー チッテーナ パマーデーナ マヤー カタン アッチャヤン カマ メー ダンマ サンディッティカ アカーリカ</dd>
    <dd class="raihai-wabun">実証できる永遠の真理たる仏法に対し、私が怠りによって犯した身口意の過ちを懺悔いたします。</dd>
    <dt class="raihai-pali">Kāyena vācā cittena pamādena mayā kataṃ Accayaṃ khama me Saṅgha supaṭipanna anuttara.</dt>
    <dd class="raihai-yomi">カーイェーナ ワーチャー チッテーナ パマーデーナ マヤー カタン アッチャヤン カマ メー サンガ スパティパンナ アヌッタラ</dd>
    <dd class="raihai-wabun">無上なる正道を実践する僧団に対し、私が怠りによって犯した身口意の過ちを懺悔いたします。</dd>
  </dl>


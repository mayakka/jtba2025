<section id="sect-1" class="sutta-box-single">
  <h1 class="headline-sutta-single">慈経 <span class="small-eng">Metta Suttam</span></h1>

  <ul class="sutta-order">
    <li class="sutta-numb">1.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali">Karaṇīyam atthakusalena yan taṃ santaṃ padaṃ abhisamecca;</dt>
        <dd class="raihai-yomi">カラニーヤ マッタクサレーナ ヤン タン サンタン パダン アビサメッチャ</dd>
        <dt class="raihai-pali">Sakko ujū ca sūjū ca suvaco c'assa mudu anatimānī.</dt>
        <dd class="raihai-yomi">サッコー ウジュー チャ スージュー チャ スワチョー チャッサ ムドゥ アナティマーニー</dd>
        <dd class="raihai-wabun">［解脱という］目的をよくわきまえた人が、静かな場所へ行ってなすべきことがあります。何事にもすぐれ、しっかりして、まっすぐでしなやかで、人の言葉をよく聞き、柔和で、高慢でない人になるように。</dd>
      </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">2.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali">Santussako ca subharo ca appakicco ca sallahukavutti;</dt>
        <dd class="raihai-yomi">サントゥッサコー チャ スバロー チャ アッパ キッチョー チャ サッラフカ ヴッティ</dd>
        <dt class="raihai-pali">santindriyo ca nipako ca appagabbho kulesu ananugiddho.</dt>
        <dd class="raihai-yomi">サンティンドゥリョー チャ ニパコー チャ アッパ ガッボー クレース アナヌギッドー</dd>
        <dd class="raihai-wabun">足ることを知り、手が掛からず、雑務少なく、簡素に暮らし、諸々の感覚器官が落ち着いていて、賢明で、裏表がなく、在家に執着しないように。</dd>
      </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">3.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali">Na ca khuddaṃ samācare kiñci yena viññū pare upavadeyyuṃ;</dt>
        <dd class="raihai-yomi">ナ チャ クッダン サマーチャレー キンチ イェーナ ヴィンニュー パレ－ ウパワディッユン</dd>
        <dt class="raihai-pali">sukhino vā khemino hontu sabbe sattā bhavantu sukhitattā.</dt>
        <dd class="raihai-yomi">スキノー ワー ケーミノー ホントゥー サッベー サッター バワントゥ スキタッター</dd>
        <dd class="raihai-wabun">智慧ある識者たちが批判するような、どんな小さな過ちも犯さないように。幸福で平安でありますように。生きとし生けるものが幸せでありますように。</dd>
      </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">4.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali">Ye keci pāṇabhūt'atthi tasā vā thāvarā vā anavasesā;</dt>
        <dd class="raihai-yomi">イェー ケーチ パーナ ブータッティ タサー ワー ターワラー ワー アナワセーサー</dd>
        <dt class="raihai-pali">dīghā vā ye mahantā vā majjhimā rassakā aṇuka_thūlā.</dt>
        <dd class="raihai-yomi">ディーガー ワー イェー マハンター ワー マッジマー ラッサカーヌカ トゥーラー</dd>
        <dd class="raihai-wabun">いかなる生命であろうともことごとく、動き回っているものでも、動き回らないものでも、長いものでも、大きなものでも、中くらいのものでも、短いものでも、微細なものでも、巨大なものでも、</dd>
      </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">5.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali">Diṭṭhā vā ye vā addiṭṭhā ye ca dūre vasanti avidūre;</dt>
        <dd class="raihai-yomi">ディッター ワー イェー ワ アッディッター イェー チャ デゥーレー ワサンティ アヴィデゥーレー</dd>
        <dt class="raihai-pali">bhūtā vā sambhavesī vā sabbe sattā bhavantu sukhitattā.</dt>
        <dd class="raihai-yomi">ブーター ワー サンバウェースィー ワー サッベー サッター バワントゥ スキタッター</dd>
        <dd class="raihai-wabun">見たことがあるものもないものも、遠くに住むものでも、近くに住むものでも、既に生まれているものも、卵など、これから生まれようとしているものも、生きとし生けるものが幸せでありますように。</dd>
      </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">6.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali">Na paro paraṃ nikubbetha nātimaññetha katthacinaṃ kañci;</dt>
        <dd class="raihai-yomi">ナ パロー パラン ニクッベーター ナーティ マンニェータ カッタチナン カンチ</dd>
        <dt class="raihai-pali">vyārosanā paṭighasaññā nāññamaññassa dukkham iccheyya.</dt>
        <dd class="raihai-yomi">ビャーローサナー パティガ サンニャー ナーンニャマンニャッサ ドゥッカ ミッチェッヤ</dd>
        <dd class="raihai-wabun">どんな場合でも、ひとを欺いたり、軽んじたりしてはいけません。怒鳴ったり、腹を立てたり、お互いにひとの苦しみを望んではいけません。</dd>
      </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">7.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali">Mātā yathā niyaṃ puttaṃ āyusā ekaputtam anurakkhe;</dt>
        <dd class="raihai-yomi">マーター ヤターニヤン プッタン アーユサー エーカ プッタマヌラッケー</dd>
        <dt class="raihai-pali">evam pi sabbabhūtesu mānasam bhāvaye aparimāṇaṃ.</dt>
        <dd class="raihai-yomi">エーワンピ サッバ ブーテース マーナサン バーワイェー アパリマーナン</dd>
        <dd class="raihai-wabun">あたかも母が、たった一人の我が子を、命がけで守るように、そのようにすべての生命に対しても、無量の［慈しみの］心を育ててください。</dd>
      </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">8.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali">Mettañ ca sabbalokasmiṃ mānasam bhāvaye aparimāṇaṃ;</dt>
        <dd class="raihai-yomi">メッタン チャ サッバ ローカスミン マーナサン バーワイェー アパリマーナン</dd>
        <dt class="raihai-pali">uddhaṃ adho ca tiriyañ ca asambādhaṃ averaṃ asapattaṃ.</dt>
        <dd class="raihai-yomi">ウッダン アドー チャ ティリヤン チャ アサンバーダン アベーラン アサパッタン</dd>
        <dd class="raihai-wabun">慈しみの心を一切世間（すべての生命）に対して、限りなく育ててください。上に、下に、横（周り）に［棲む如何なる生命に対して］も、わだかまりのない、怨みのない、敵意のない心を育ててください。</dd>
      </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">9.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali">Tiṭṭhaṃ caraṃ nisinno vā sayāno vā yāvat'assa vigatamiddho;</dt>
        <dd class="raihai-yomi">ティッタン チャラン ニスィンノー ワー サヤーノー ワー ヤーワタッサ ヴィガタミッドー</dd>
        <dt class="raihai-pali">etaṃ satiṃ adhiṭṭheyya brahmam etaṃ vihāraṃ idham āhu.</dt>
        <dd class="raihai-yomi">エータン サティン アディッテェッヤ ブラフマメータン ヴィハーラン イダマーフ</dd>
        <dd class="raihai-wabun">立っている時も、歩いている時も、坐っている時も、あるいは横になっていても眠っていない限り、この［慈悲の］念をしっかり
          保っていてください。これが梵天（崇高なもの）の生き方であると言われています。</dd>
      </dl>
    </li>
  </ul>

  <ul class="sutta-order">
    <li class="sutta-numb">10.</li>
    <li class="sutta-cont">
      <dl>
        <dt class="raihai-pali"> Diṭṭhiñ ca anupagamma sīlavā dassanena sampanno;</dt>
        <dd class="raihai-yomi">ディッティン チャ アヌパガンマ スィーラワー ダッサネーナ サンパンノー</dd>
        <dt class="raihai-pali">kāmesu vineyya gedhaṃ na hi jātu gabbhaseyyaṃ punaretī ti.</dt>
        <dd class="raihai-yomi">カーメース ヴィネッヤゲーダン ナヒジャートゥ ガッバ セッヤン プナレーティー ティ</dd>
        <dd class="raihai-wabun">［このように実践する人は］ 邪見を乗り越え、常に戒を保ち、正見を得て、諸々の欲望に対する執着をなくし、もう二度と母体に宿る（輪廻を繰り返す）ことはありません。</dd>
      </dl>
    </li>
  </ul>

  <h2 class="hl-kugiri">祈願文 <span class="small-eng">Patthanā</span></h2>

  <dl>
    <dt class="raihai-pali">Etena sacca vajjena pātu naṃ ratanattayaṃ</dt>
    <dd class="raihai-yomi">エーテーナ サッチャ ワッジェーナ パートゥ ナン ラタナッタ ヤン</dd>
    <dd class="raihai-wabun">この真理の言葉の力によって我らは三宝に守られますように。</dd>
    <dt class="raihai-pali">Etena sacca vajjena hotu no jaya maṅgalaṃ.</dt>
    <dd class="raihai-yomi">エーテーナ サッチャ ワッジェーナ ホートゥ ノー ジャヤ マンガラン</dd>
    <dd class="raihai-wabun">この真理の言葉の力によって我らは幸福でありますように。</dd>
    <dt class="raihai-pali">Etena sacca vajjena sadā sotthi bhavantu no.</dt>
    <dd class="raihai-yomi">エーテーナ サッチャ ワッジェーナ サダー ソッティ バワントゥ ノー</dd>
    <dd class="raihai-wabun">この真理の言葉の力によって我らは常に安穏でありますように。</dd>
  </dl>
  <p class="sutta-add-exp">※ 護経として読経する場合は以下の文言を唱える。</p>
  <dl>
    <dt class="raihai-pali">Etena sacca vajjena pātu tvaṃ ratanattayaṃ</dt>
    <dd class="raihai-yomi">エーテーナ サッチャ ワッジェーナ パートゥ トゥワン ラタナッタ ヤン</dd>
    <dd class="raihai-wabun">この真理の言葉の力によってあなた（がた）に三宝のご加護がありますように。(三唱) </dd>
  </dl>


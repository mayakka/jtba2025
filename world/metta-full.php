    <p class="label-kaisetu">解説</p>
    <p>「慈悲の実践」のフルバージョンを紹介します。いままで皆様がたが実践してきた慈悲の瞑想は、慈悲喜捨の気持ちを凝縮した省略バージョンです。それは誰にでも簡単に暗記できる「慈悲の携帯バージョン」です。慈しみの実践をするたびに、こころがその影響を受けて、変わっていかなくてはいけないのです。慈悲の実践は、人間を優れた人格者に育てるための方法なのです。慈悲の省略バージョンを実践することで、人格が変わって幸福になった人々はたくさんいるようです。同時に、慈悲の実践のインパクトを感じていない方々もいるのです。</p>

    <p>そこで、こころに確実に影響を与えることを狙って、慈悲の実践のフルバージョンを作成してみたのです。経典でお釈迦さまが慈悲について語られているところを沢山参考にしたのです。パーリ語のフレーズは、直訳してみてもうまく通じないところも出てきます。そのような箇所を日本語で通じるように編集しなくてはいけないのです。そこは私の能力が及ばないところなので、自信はありません。何とかまとめられた、という気持ちです。これから皆様の意見も聞いて、さらに文章を編集したいと思っているのです。</p>

    <p>慈悲の気持ちを経験してほしいのです。体感してほしいのです。こころにインパクトを与えて、こころを変えてほしいのです。こころは洗脳しやすい、脆いものです。人間のこころは貪瞋痴で汚れているのです。我々のこころは世間の貪瞋痴のこころによって洗脳されているので、たいへん不幸な人生を過ごさなくてはいけなくなっているのです。慈悲の実践で完全に清らかなこころを育てるのです。世間の波に左右されない、汚れたこころで、迷信で、脅迫で洗脳されない、安定したこころを育てるのです。こころを治せば、人生は奇跡的に変わります。人生における本物の奇跡とは、慈悲です。それを体感してみると、「慈悲は奇跡だ」と各自で発見するに違いありません。</p>

    <p>ここで紹介する文章を自分のこころで感じながら、念じてみてください。自分の好きな節にあわせて唱えてみてください。これは慈悲のフルバージョンなので、すべてを唱えるために時間がかかるかもしれません。唱えている最中、自分のこころで感じやすいセットを見つけたならば、繰り返し三回くらい唱えてみてください。すべての言葉を三回唱えることを考えると、時間がかかるかもしれませんが、しっかりと慈悲の瞑想をしたことにはなります。</p>

    <p>忙しいときは、自分の気に入ったセット一つ二つを選んで、唱えることもよいのです。何日か実践してみると、自分が一番気にいるフレーズが見つかるのです。それが自分専用の慈悲の瞑想になります。</p>

    <p>ヴィパッサナー実践以外の瞑想は、基本的に何かのフレーズを唱えることで成り立つのです。このフレーズが自分の性格に合わない場合は、瞑想は進みません。自分特有の慈悲の瞑想を発見したら、それをしっかりと、こころに携帯してください。自分のモットーにしてください。毎日、幸福に生きられるようになります。予測できなかった出来事に遭遇しても、ものの見事に正しく対応できるようになります。怒り・嫉妬・落ち込みなどの感情が消えて、微笑んで生きられるようになります。自我の錯覚がじわじわと消えてゆくことを経験できます。ブッダの説かれた世界は、幸福に満たされた世界であることを発見します。</p>

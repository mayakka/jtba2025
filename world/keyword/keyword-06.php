    <p>dāna（ダーナ）は「あげる」「与える」ということで、英語の give です。「お布施」という言葉からは宗教的な寄付行為を連想しますが、ダーナはもっと単純で広い意味です。誰かに何かを与えることはすべてダーナになります。知識のある人が何かを教えてあげたり、時間のある人がその時間を使って誰かの役に立つこともダーナです。人は他人に何かを与えることによって社会の中で役に立つ存在となり、その生き方が意味のあるものとなります。そして、「しっかりしないといけない」と自分の行動も守られるのです。ですからdānaは、在家の人々がまずなすべき道徳です。<br>
      ダーナの徳の高さには段階があります。ダーナには、する人と、される人と、するものの三つがありますが、それらは清らかであればあるほど徳の段階が高いのです。</p>

    <p>仏教ではいつでも、心が清らかであるかどうかを最も大切なことと考えます。心が清らかな人に布施をすると、布施をした人の心が洗われます。布施を強要したり、布施が少ないなどと怒ったり差別をしたりするような人に布施をしても心は清らかにはなりません。布施は「ほめてもらおう」という見栄や感情的な同情心ではなく、「ない人にはあげる」というだけのシンプルな気持ちでするのがいちばん徳が高いのです。そこには何も判断や選別はありません。<br>
      いくら布施をしても、自我が膨らんで「私はこういうこともした、ああいうこともした」と考えてばかりいるならば、それは善行為とはいえません。貧しい人を助けてあげても「やっぱり皆同じだから助けないといけないし、たまたま私にできるからしているだけだ」と自我がどんどん消えていって「いいことができてよかった」という感じで終わるならば、すばらしい善行為になります。<br>
      なぜそういうことを最も大切に考えるかというと、汚れた思考を育てると悟りの道となんの縁もなくなってしまうからです。いくらお布施をしても、心が清らかな方へ、智慧の方へ進まないならばその行為にはなんの意味もありません。仏教ではいつでも智慧の方へ、自我をなくす方へ進んでいるかどうかということを、一番大切にするのです。</p>


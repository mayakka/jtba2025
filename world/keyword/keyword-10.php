    <p>sobhana は「善美の、浄き」という意味で、sobhana-cetasika は浄心所と訳されています。<br>
      浄心所とは心を清らかにして向上させる心のはたらきです。「心の汚れを洗って、心を清らかにしてください」ということは仏教の最も基本的な教えです。では、いったいどういうはたらき（心所）のある心が清らかな心なのでしょうか。アビダルマではこの疑問について「浄心所」できちんと説明しています。浄心所は全部で25種類あります。</p>


        <p class="pali-large"><span class="num-g">19．</span>cittujukatā</p>
      </div>
      <p>「ちゃんとまっすぐ」ということ。きちんと決めた行動をすること。心が揺れない。イヤイヤではなくて、「やり終わりました」ときちんと全部やって終わることです。</p>
    </section>

    <p class="key-matome">浄心所は全部で25ありますが、以上の19の浄心所は常に同時に働くので特に、共浄心所と呼ばれています。善心による行動はストレスが溜まらず疲れませんし、後から思い出してもとても気持ちがいいものです。人生を成功したければ、浄心所を育てなければなりません。「善い心が働けば明るく行動的になる」ということを忘れずに浄心所のはたらきに沿う行為をします。特別な宗教的な行動は、ひとつもありません。仏教は普遍的な教えであって、宗教、民族、時代を超えて、誰にも役に立つ教えなのです。</p>

    <p class="group-lead"><span>離心所（３種類）</span></p>

    <section>
      <div id="sect-20" class="key-term-sup">
        <h2 class="key-term">サンマーワーチャー：正語</h2>
        <p class="pali-large"><span class="num-g">20．</span>sammā vācā</p>
        <p class="key-sup">正しい言葉しか語らないエネルギー</p>
      </div>
      <p>サンマーワーチャー（正語）は、正しく言葉をしゃべるときに生まれる心所です。具体的には、①嘘　②人の悪口　③人の心を傷つける言葉　④ムダ話、という四つの悪語から、離れることです。優しい言葉で、人の役に立つことをしゃべるように心がけます。</p>
      <p>しゃべるときは、サティはなかなかできません。そのときは「私はしゃべるのですから、正語をしゃべります」と、正しい言葉をしゃべります。</p>
      <p>たとえ「誰かを傷つけてやろう」という悪い意図はなかったとしても、悪い言葉を使うといい結果にはなりません。言葉というのは、言葉自体が意味を持っています。ですからその言葉の意味が自分の心に跳ね返り、心を汚してしまいます。</p>
      <p>結局、正語とは、相手のことを思いやって、話さなくてはならないことだけを、時と場合をみて話すことです。</p>
    </section>

    <section>
      <div id="sect-21" class="key-term-sup">
        <h2 class="key-term">サンマーカンマンタ：正業</h2>
        <p class="pali-large"><span class="num-g">21．</span>sammā kammanta</p>
        <p class="key-sup">正しい行為をするエネルギー</p>
      </div>
      <p>kammanta（カンマンタ）は、「体で行う行為」を指し、正業とは「社会や他の生命に対して害にならないような行為」のことです。</p>
      <p>みだりに生き物を殺す、他人のものを盗る、みだらな行為をする、という三つの悪行為から離れることです。賄賂を取ったり、大した仕事もせずに高い給料をもらったりすることも、他人のものを盗る行為です。</p>
      <p>心が清らかになれば自然に体の行動も正しくなるはずなのですが、どんな人でも完全に心をきれいにしていない限り、ある条件の中では悪い行為をしてしまう可能性をもっています。どのようなことがあっても気をつけて、社会や生命のためになるような行為を心がけます。</p>
    </section>

    <section>
      <div id="sect-22" class="key-term-sup">
        <h2 class="key-term">サンマーアージーヴァ：正命</h2>
        <p class="pali-large"><span class="num-g">22．</span>sammā ājīva</p>
        <p class="key-sup">正しい仕事をするエネルギー</p>
      </div>
      <p>社会の中で生きている私たちは、生活をするために何かの仕事をすることが必要です。仏教では、生活の糧を得るために仕事をすることは人として当然のことであり、逆にきちんと仕事をしない人のことを厳しくいさめています。</p>
      <p>正命は、人の役に立つ仕事を選び、自然や社会、生命の破壊につながらないように仕事をすることです。仕事の種類はたくさんありますので、仏教では具体的な職業を禁ずることはしていません。ただし毒、酒、武器、などを作ったり売ったりすることは禁じてあります。</p>
      <p>仕事をするときでも、できるだけ、善心所でがんばると、とてもいい結果が得られます。いい結果といっても、お金が儲かるというのではなくて、仕事をすればするほど自分の心が清らかになっていくのです。</p>
      <p>離心所の三つは、仏教徒として日常生活をどう生きていくべきかという規準になります。離心所の三つだけでも守って生活すると、かなりの浄心所が出てきます。私たちがしているすべてのこと、朝起きて、着替えて、食事をつくって…、という日常生活をすべて浄心所で励むようにしようと思えば、できるのです。がんばってそのようにすれば、とてもいい結果が得られます。</p>
    </section>

    <p class="group-lead"><span>無量心所（２種類）</span></p>

    <p class="key-matome">karunā（悲）と muditā（喜）は、一般的な善心（共浄心所）とは別に育てなければならない浄心所で、無量心所（appamaññā - cetasika）と名づけられています。無量心（appamaññā - citta ）とは、限りなく広げられ、力強くすることができる心という意味で慈悲心の別名です。</p>

    <section>
      <div id="sect-23" class="key-term-sup">
        <h2 class="key-term">カルナー：悲</h2>
        <p class="pali-large"><span class="num-g">23．</span>karuṇā</p>
        <p class="key-sup">他の悩み苦しみを助けようとするエネルギー</p>
      </div>
      <p>好きな人が苦しんでいれば共に涙を流す人でも、嫌いな人が苦しんでいたら「ざまあみろ」と考えるのはよくあることです。年寄りが病気になったと聞くと「かわいそうに」と同情する人も、自分が世話をしなければならないとなると「めんどくさい」と思ったりもします。私たちの心というのは、そのように自己中心的で恐ろしいものなのです。</p>
      <p>苦しんでいる人がいればいつでも「かわいそうだから何とかしてあげたい」と思える優しい心は自然には生じないので、がんばって育てる必要があります。「本当はイヤなのだけど、なんとかして優しくしよう」というのではなく、自然に心からわいてくるエネルギーでなければ意味がありません。そのためには、まず、「自分の苦しみがなくなりますように」「自分の親しい人の苦しみがなくなりますように」と念じるところから始めて、徐々にその心を広げていきます。</p>
      <p>他の生命の苦しみを感じられる人間になる。人の悩み苦しみを理解し、心配し、「何とかなってほしい、何かしてあげたい」と自然に正直に思える心を育てる。そうすると醜くて汚い小さな心が、美しく香り高い大きな心になっていきます。このエネルギーは無限に育てることができます。</p>
    </section>

    <section>
      <div id="sect-24" class="key-term-sup">
        <h2 class="key-term">ムディター：喜</h2>
        <p class="pali-large"><span class="num-g">24．</span>muditā</p>
        <p class="key-sup">他の幸福を喜ぶエネルギー</p>
      </div>
      <p>人は、誰かが成功すると、何となくおもしろくないと感じて嫉妬します。ムディターは、嫉妬の反対で、相手の成功を共に喜ぶ気持ちです。他人が幸福であるのを見て共感する優しさです。</p>
      <p>カルナーと同様にムディターも自然な心です。人は無理矢理に義務感で喜ぶことはできません。母親が子供の幸福を見ると即座に喜びの心がわいてくるような、自然に出てくるエネルギーを育てるようにします。まず自分の幸福、自分の親しい人々の幸福を喜んで自然な気持ちを感じてみて、その気持ちをどんどん広げ育てていくのです。ムディターを育てることも、小さな心を大きくすることであって、それは無限に大きくすることができます。</p>
      <p>慈悲心（無量心）は限りなく育てられます。仏教で言う有情（生命）には、私たちの目に見える生命だけではなく、梵天や神々や地獄の生命などすべての生命が含まれています。それらすべての生命が幸福になりたいと願い、優しさを求めているのです。慈悲心は、そういう無限（無量）の生命を対象としているので、無制限に拡げていくことができるのです。もちろん私たちが実際に行動する時には限られた対象にしか優しくすることはできません。これはあくまでも心の状態のことです。人は限りなく優しい心を育てることができるのです。</p>
    </section>

    <p class="key-matome">無量心は、mettā（慈／メッター）、karunā（悲／カルナー）、muditā（喜／ムディター）、upekkhā（捨／ウペッカー）の四つです。その中のメッターとウペッカーは、共浄心所の adosa（無瞋）と tatramajjhattatā（中捨）にそれぞれ含まれています。慈しみの心（mettā）と平等である心（upekkhā）は善行為には常に必要なはたらきですが、必ずしも他の幸不幸に関与していくはたらきではありません。
      カルナーとムディターは、他の生命の幸不幸に関わりをもっていく善心所です。私たちは生きている限り、他の生命とつながって存在しています。ですからカルナーとムディターを育てることはとても大切なことです。慈悲の心で生きることこそ、この世で最高に幸福な生き方なのです。</p>

    <p class="group-lead"><span>智慧の心所（１種類）</span></p>

    <section>
      <div id="sect-25" class="key-term-sup">
        <h2 class="key-term">パンニンドゥリヤ：慧根</h2>
        <p class="pali-large"><span class="num-g">25．</span>paññindriya</p>
        <p class="key-sup">智慧、ものごとの真理の側面を観ることができるはたらき</p>
      </div>
      <p>仏教で一番大事なことは、paññā（パンニャー：智慧）が現れることです。仏教で言う智慧とは、無常・苦・無我・因縁の法則などの真理を正しく観ることができるはたらきです。智慧は、共浄心所（一般的な浄心所）とは別に育てなくてはいけない浄心所です。つまり、善心で生きているからといって智慧が生じるわけではないということです。そのことを理解しておいてください。</p>
      <p>ものごとが永久的に続くと思うのは、無知ゆえです。ものごとは瞬間しか成り立たないという無常の真理を理解し、その体験へ進む方法は、智慧を発達させる道です。</p>
      <p>たとえば人に優しくすることは善いことですが、そこで喜んで終わるのではなく、智慧も育ててもらいたいのです。どういうことかというと、善いことをしても「これはこの瞬間だけのことだ、すべてはすぐに消えていく」という無常の方向で考えてみるのです。そういうように考えると、智慧が生まれてきます。人に優しくしたからといって、その人を永久的に助けてあげたわけではないし、自分が永久的な何かを得たわけではありません。</p>
      <p>たとえば、人に夕食をごちそうしてあげたとします。それでその人が喜んだとし、ても、ずっとその人を追いかけて「私はあなたをこの間喜ばせてあげたでしょう。あなたは満腹したでしょう」などと言いつづけるのはおかしいでしょう。</p>
      <p>けれども無知な人の行動はそれと似ていますね。<br>
      ですから無知の人がいくら善いことをしても、力強い善い結果は得られないのです。たとえ大金を寄付しても、その根底が無知であれば、わずかな善果で終わってしまいます。智慧がある人が善いことをすれば、確実に善い結果が得られます。</p>
      <p>私たちにも時たま智慧は生まれます。「世の中は無常だ」と思ったりするのは智慧なのです。しかし慧根はなかなか根づきません。心の中に無知という巨大な木があり、ものすごく広く深く根が張っていると考えてください。その痴根の代わりに慧根を根づかせなければならないのです。</p>
      <p>まず無知の木のそばに智慧の木の種を植えます。<br>
      種を植えても、大きな木のそばだから、日当たりは悪いし、育ちにくくて、なかなか根づきません。ですからずっと見守ってあげて、日が当たるようにしてあげたり、水をやったり、いろいろと世話をしてあげるのです。そうすると芽が出て、少しずつ根を張っていくのです。</p>
      <p>智慧の木は、たくさんの浄心所がついているから力強いのです。無知の木は大きいけれども、智慧の木よりも弱いのです。ですから智慧がある程度大きくなると、無知の木は倒れてしまいます。その代わりに智慧の根がきちんと定着します。そのようにして智慧で無知を追い払うのです。ですから私たちの仕事は、無知をなくそうとするのではなく、智慧を育てようとすることです。そうすれば無知の大木もいずれ弱くなって死んでしまいます。</p>
      <p>ものごとは、ものすごいスピードで変化しています。それを頭だけで理解しても、心の中では「ものごとはずっと有るのだ」と思ってしまいます。心の波動はあまりにも速いのです。スピードが速ければ速いほど、そこに何かが「有る」ように思えます。<br>
      「魂が有る」「私がいる」などと思ってしまうのは心があまりにも無常だからです。実際は、人間の心もすべての現象も、流れている川のように瞬間瞬間変化しつづける実体のないものなのです。</p>
      <p>慧根（paññindriya）は、そういうことが頭でわかるのではなく、修行（ヴィパッサナー）による体験でしっかりとわかることによって生じるのです。</p>
    </section>


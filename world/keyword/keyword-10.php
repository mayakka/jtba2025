    <section>
      <div id="sect-1" class="key-term-sup">
        <h2 class="key-term">サッダー：信</h2>
        <p class="pali-large"><span class="num-g">１．</span>saddhā</p>
        <p class="key-sup">自由で正しい判断による確信</p>
      </div>
      <p>サッダー（信）は、自分の自由で正しい判断によって何かを信頼したり信じたりすることです。<br>
      サッダーは心の汚れ（欲や怒りなど）を沈めて心をきれいにします。それは人を明るく活発に行動させるための基本的な心の状態です。でもいくら行動的になったとしても、何か悪い行為、たとえば他の生命を害したり何かを盗んだりするのであれば、それはサッダーによる行為ではありません。世の中に活発に活動しているように見える人々はたくさんいますが、ほとんどの人々は自分の欲や怒りのエネルギーで行動しています。行動の動機が欲や怒りであれば、その行動は当然悪い行為になります。サッダーによる行動かどうかは、自分も周りも明るく幸福になる行為をしているかどうかで判断することができます。</p>
      <p>仏教では「仏法僧（三宝）を信頼して、道を歩んでください」と言っています。仏とは自ら悟りを開かれたお釈迦さまのこと、法はお釈迦さまが説かれた真理、僧はお釈迦さまの教えによって悟りを開いた人々です。三宝を信頼するためには、お釈迦さまの教えを勉強したり実践したりして、自分で確かめて納得することが必要です。サッダーはきちんと自分で得た確信です。何も実践しないでただ信じるだけでは、しっかりした確信にはならないのです。</p>
    </section>

    <section>
      <div id="sect-2" class="key-term-sup">
        <h2 class="key-term">サティ：念</h2>
        <p class="pali-large"><span class="num-g">２．</span>sati</p>
        <p class="key-sup">気づき、今の自分の状態や自分が置かれている状況に目覚めていること</p>
      </div>
      <p>私たちは皆、眼耳鼻舌身意という六つの認識作用から得られる情報に、振り回されて生きています。それは実際ひどい状態なのですが、私たちはそのことにあまりにも慣れてしまって、その事実にさえ気がついていません。例えばある音が聞こえたとすると、私たちは聞いた瞬間に「好きなタイプの古いジャズだ」「お母さんが怒っている」などと判断し、執着したり嫌ったりして、欲や怒りの煩悩で心を汚します。これは、音だけに限りません。見るもの、食べるもの、匂い、感じること、考えること、妄想すること、等々、ありとあらゆることによって、私たちは煩悩を作り続けています。サティ（気づくこと）はその束縛を解き、心を自由にします。例えば電車の中で足を踏まれたら、「痛い」と思った瞬間に怒りが生じて、心は汚れていきます。もしも、サティをもって「痛み」とその現象を観ると、怒りは生じません。それどころか、すばらしい智慧が生じてきます。そのように、サティは不善に走る心を善に入れ替えるすばらしい力を持っているのです。</p>
      <p>人を親切に助けてあげるような善行為の場合も、サティを入れたらよりすばらしい行為になります。普通そういう善行為をすれば、どこかに自我が残ります。お礼を言われることを期待したりして、よけいな煩悩が心に残るのです。サティを入れて人を助けてあげれば「私が」という自我なしに人を助けるので、何も心に残りません。それは純粋に清らかで真にすばらしい行為となります。</p>
      <p>ほんの瞬間でもサティを入れたならば、そこに善があるのです。ですからサティはがんばってできるだけたくさん入れれば入れるほどいいのです。１日や２日ではなく、毎日毎日一生続けてくださいと仏教では言っています。痛くなったら「痛み」、怒ったら「怒り」、何か聞こえたら「音」と気づく。特別なことをするのではなく、普通の生活をしながらサティを入れればいいのです。そうすればサティを入れる瞬間瞬間、善行為をしていることになります。それを積み重ねていくと、いろいろな対象から自分がどんどん自由になっていって特別な智慧が生じてきます。サティこそは智慧を生じさせ、解脱に至る道に導いてくれる大切なはたらきなのです。</p>
    </section>


    <section>
      <div id="sect-4" class="key-term-sup u-mt-10">
        <h2 class="key-term">オッタッパ：愧</h2>
        <p class="pali-large"><span class="num-g">４．</span>ottappa</p>
        <p class="key-sup">不善行為をすることを怖がること</p>
      </div>
      <p>「慚と愧の二つは世の中の支配者です」という言葉があります。それほどこの慚と愧は大切なはたらきなのです。「こういう悪いことをすることは自分に対して恥ずかしい」「こんなことをして人に知られたら怖い」という二つの心によって、この世の道徳秩序は守られています。この二つがなくなったら恐ろしい世界が現れて、もうこの世の終わりだと言ってもいいでしょう。</p>
      <p>この二つのはたらきが弱くなると人は悪いことをしてしまいます。たとえばある大学の実力者が裏口入学を頼まれたとします。五万や十万のお金ではその人の心は動かないでしょう。「そんなことをしたらよくない、怖い」という慚と愧がちゃんとはたらきます。ところが「一千万でも、いえ五千万円でも渡しますよ」と言われると、もしかすると慚と愧が弱くなって不正をしてしまうかもしれません。そのように私たちの心は善と不善が戦っていて、場合と条件によって悪いことをしてしまうのです。けれども慚と愧をしっかりと育てている人は、決して悪いことはしません。</p>
      <p>慚と愧が育っている人は、「私はこれをします」と堂々と行動します。どっちつかずのもやーっとした心ではなく、やってはいけないこととやるべきことの区別がしっかりできるようになるのです。ですから慚と愧の浄心所としてのはたらきは、正しい決断力の伴った行動的な力を私たちにもたらすことです。</p>
    </section>

    <section>
      <div id="sect-5" class="key-term-sup">
        <h2 class="key-term">アローバ：無貪</h2>
        <p class="pali-large"><span class="num-g">５．</span>alobha</p>
        <p class="key-sup">欲から離れるエネルギー</p>
      </div>

      <p>無貪（不貪とも言います）は、欲から離れることです。人間にとって、それは非常に難しくてやりにくいことなのです。人は好ましい物、知識、人など、何かを得ることが幸せだと思っていますから「手放してください」と言われたら、どうしても逆らってしまいます。「私たちは何かにつかまって安定しているのに…」と不安になるのです。</p>
      <p>お釈迦さまは「欲しい」という貪りのエネルギーによって幸福になることは決してありません」とおっしゃっています。人は何かを得る度に「もっと」という不満が心に残ります。「欲しい」という心は限りなく生じ続けるので、不満も無限に生じて人を苦しめます。そこには不安、孤独、虚しさ、怖れ、暗さ、などたくさんの不幸がついてきます。</p>
      <p>実際に手放してみると、いろいろなものを手放せば手放すほど自由で楽になることを経験できます。たくさんのものを背負っていると歩きにくいですが、荷が軽いと軽々と歩けます。それと同じことです。一度に全部捨てることが難しければ、少しずつ手放して前進していけばいいのです。</p>
      <p>心が身軽になる方法論として、仏教では「布施（dāna）を実践してください」と言っています。物、時間、体力、知識など、なんでも自分のもっているものを他の人ために使って他に与えるのが布施行です。人間というのは「あげるよりもらいたい」という心が普通です。ですから施すエネルギーは、「布施を実践しよう」と無理にでも努力して育てる必要があります。</p>
      <p>『貪』の反対は『布施』ではないかと思われるかもわかりませんが、仏教では『無貪（アローバ）』という否定形の言葉でこの浄心所を表しています。布施（dāna）にはあげる対象がありますから、どのくらい「物」を持っているかということによって「制限」があります。無貪（欲から離れること）というと、心の欲を捨てることですから、完全に清らかになるまで捨てられますので、「制限」ということは成り立ちません。解脱に至るまで大きく育つことができます。究極的な施し（布施）は自分の命を他のために与えることですが、たとえ命を人に与えても、それは欲を完全に捨てたことにはなりません。ですので悟ることはありません。けれども無貪（アローバ）を育てていけば解脱にまで至ることができます。仏教では、そういう微妙なところまで気をつけて厳密に言葉を使っているのです。</p>
    </section>

    <section>
      <div id="sect-6" class="key-term-sup">
        <h2 class="key-term">アドーサ：無瞋</h2>
        <p class="pali-large"><span class="num-g">６．</span>adosa</p>
        <p class="key-sup">認識する対象を否定しないこと</p>
      </div>
      <p>アドーサ（adosa）は、ドーサ（dosa：瞋）に否定形のア（a）がついた言葉です。ドーサは、自分の認識した対象を否定し、対象を嫌うことです。人は皆、自分にとってマイナスであるものを嫌ったり、怒ったり、嫉妬したり、憎んだり、恨んだりします。自分にとって都合の悪い人を嫌うことは、嫌わないことに比べていかに簡単であることか…私たちはそういう恐ろしい心で生きています。</p>

      <p>仏教では「世の中の人々に対してはもちろんのこと、すべての生きとし生けるものに対して限りなく優しさをもつべきですよ」と言っています。「いい人には慈しみを持つが、悪い人はつぶす」という世の論理は、正しい論理ではありません。悪い人は物事がわかっていない人（目が見えない人）なのだから、逆にもっと優しくしてあげなくてはいけないのです。いくら相手の性格や、しゃべること、することなどが、自分にとって都合が悪くても、「私は嫌いになりません」と平然としていた方がいいのです。誰かを嫌いになったら、その人のことを考える度に自分が暗くなって、自分の心が汚れてしまいます。怒りは猛毒です。どのような場合でも、怒りを正当化しないことです。</p>

      <p>「相手がどうであろうとあっけらかんとして、何を言われてもきれいな心でニコニコしている人こそ勝者ですよ」と仏教では言っています。自分が幸せになりたければ、そういうしっかりした人間になることです。腰抜けの中途半端な状態で幸せになることはできません。「あなたの心は汚れています。それを洗うための一番強力な洗剤は慈しみの心ですよ」と仏教では言います。</p>

      <p>慈しみの心を育てる一番いい方法は、慈悲の瞑想をすることです。「私は幸せでありますように私の親しい人々は幸せでありますように。生きとし生けるものは幸せでありますように」と正直な心で念じます。きれいな正直な心で慈悲の瞑想を続けていると、自分の主観で自分中心に世の中を見る自我がどんどん薄くなっていきます。今まで世の中に対して自分が作っていた感情ではなく、まったく違う感情が生まれてきます。慈悲が成長したということは、その人がまったくの別人に生まれ変わることなのです。お釈迦さまは、「慈悲の心のある人は梵天そのものですよ」とおっしゃっています。心に慈しみさえあれば、私たちはたいへん尊い神々のように生活することができるのです。</p>
    </section>

    <section>
      <div id="sect-7" class="key-term-sup">
        <h2 class="key-term">タトラマッジャッタター：中捨</h2>
        <p class="pali-large"><span class="num-g">７．</span>tatramajjhattatā</p>
        <p class="key-sup">認識する対象に対してバランスのとれた客観的な態度をとるはたらき</p>
      </div>
      <p>タトラは「そこに」マッジャッタターは「中立的、かたよらない」という意味で、今認識している対象に対して客観的で冷静な態度を取るということです。私たちが何かを正しくする時は、かたよった態度ではできません。自分の好き嫌いなど主観的な感情を置いておいて、冷静に落ち着いた態度で行動しなければなりません。タトラマッジャッタター（中捨）とはそういう平安な心です。</p>
      <p>このことを知っている人は、中捨という浄心所を成長させようとします。善行為をするときも、「やるべきことだからやります」という感じで、感謝や感動を求めません。それよりも自分の平安で清らかな心が成長することを喜びます。</p>
      <p>アビダルマでは、慈悲喜捨の捨の心（upekkhā）をこの心所に含めています。中捨が強くなると、主観的な自分主義がまったく消えて、生命は皆生命として平等だよ、という捨（ upekkhā ）の心になるのです。優れているとか優れていないとか、そういうのは無知の人々の言う言葉で、仏教は生命に対してそういう差別はしません。ウペッカーは、智慧によって生命を理解して、その理解によって相手に対して落ち着いているという、とてもレベルの高い優しさなのです。</p>
    </section>


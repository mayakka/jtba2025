      <p>善悪を指す言葉は仏教では二通りあります。ひとつは<b>クサラ（kusala）・アクサラ（akusala）</b>、もうひとつは<b>プンニャ（puñña）・パーパ（pāpa）</b>です。クサラとプンニャは善、アクサラとパーパは不善です。</p>

      <p><b>プンニャ</b>というのは宗教的な善い行為で「徳、功徳」という意味です。仏教では、功徳を積んだ人は確実にいい結果、幸福が得られると教えています。その反対の<b>パーパ</b>という言葉は、もともと「捨てられる、否定される」という意味で、やってはいけないと決められている罪を指します。人を殺したり泥棒をしたり、パーパをすれば確実に悪い結果になります。仏教では、殺生、盗み、邪な行為、偽り、無駄話、悪口、二枚舌、貪欲、嗔恚、邪見の十悪がパーパとしてよく経典に出ています。そのなかでも最後の３つ、異常な欲と異常な怒りと邪見の３つが最も罪の重いパーパであるとされています。
      </p>
      <p><b>クサラ</b>というのは善行為といっても、「上手」「巧み」ということで、<b>アクサラ</b>というのはその反対。つまり「へた」ということです。それくらい軽い意味なのです。巧みと言ってもすごく大胆な意味でもなくて、広い意味での善行為。幸福をもたらす行為がクサラで、不幸をもたらす行為はアクサラ。生き方がへただと不幸ですよ、というくらいの意味なのです。
      </p>
      <p>ですから、すべてのプンニャ（徳）はクサラ（巧みな行為）に含まれますが、すべてのクサラがプンニャではありません。たとえば勉強ができることはクサラですが、それによって来世も幸せになるとは限りません。ですからクサラとプンニャはまったく違うわけではありませんし、完全にイコールというわけでもありません。プンニャはカルマとしていい結果が得られるくらいの力を持っている行為です。
      </p>
      <p>経典ではふつうはプンニャ（徳)よりもクサラ（巧み)という言葉をよく使います。なぜならあまりプンニャについてしゃべると、宗教的になりすぎるのです。仏教では宗教的な話はなるべく控えています。徳を積まなければいけない等と話しすぎると、すごく厳しくなってしまうのです。原理主義のようになればかえって問題が出てくることもあります。ですから、何をしてはいけないかということをどうしても教えないといけないときのみプンニャ・パーパという言葉が使われています。
      </p>
      <p>解脱に至るまでの行為はクサラです。人生を本当に巧みに運ぶ人は、解脱まで行ってしまうのです。クサラッサ ウパサンパダー（kusalassa upasampadā)というのは「クサラに行ってください、クサラに入ってください」ということです。ただ「悪いことをするなかれ、善いことをせよ」ということでもないのです。「悪いことをするなかれ」というのは、変なことはしないというだけのことでいいのですが、「善行為をしなさい」というのはポジティブに、進んで行為することを意味します。「巧みな生き方をしてください、巧みなシステムに入って進んでください」という意味です。
      </p>
      <p>この教えは命令形では述べられていません。「～するな、～しなさい」という言い方ではなく、「悪いことをしないこと。善い方向へ進むこと。自分の心を清らかにすること。それがブッダたちの教えですよ」とおっしゃっているのです。
      </p>
      <p>個人が自分で判断して、自己責任で正しい道を歩まなければなりません。お釈迦様がなさったのは、正しい道を提示することだけでした。</p>

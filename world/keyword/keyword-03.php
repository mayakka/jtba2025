        <h2>修行とは何か？</h2>
        <p class="pali-small">khantī, titikkhā</p>
      </div>

      <ul class="quote-container">
        <li class="q-pali">Khantī paramaṃ tapo titikkhā,<br>Nibbānaṃ paramaṃ vadanti Buddhā;</li>
        <li>
          <dl class="q-bilingual">

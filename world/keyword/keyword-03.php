      <p>これらの偈（詩句）は出家者に対する言葉です。お釈迦さまはまず「出家者は生命に対してすばらしい慈しみの心を持って、人に迷惑をかけないようにやさしく行動しなければいけません」とおっしゃっています。不思議なことですが、戒律というのは何億とあるのに、ただこのことだけを守っても全部守ることになってしまうのです。すべての生命に親切にやさしく、わずかな迷惑もかけないようにするということだけで、山のようにある戒律を包摂してしまいます。
      </p>
      <p>たとえば「出家者は人に何かをお願いしてはいけない」という戒律があります。考えてみると、それは結局人に迷惑をかけることになるのです。出家者に何かをお願いされたら、誰でも断りにくいのです。宗教家というのはどこの国でもやはり尊敬される立場にいます。たとえ陰で悪口を言っている人でも、出家者に頼まれたらたいがい言うことを聞いてしまいます。そこで出家者は人に何かを頼んではいけないという戒律があるのですが、そういうことは「生命に迷惑をかけてはいけない」という言葉にまとめられてしまいます。そういうところはお釈迦さまの言葉の本当に不思議なところです。</p>
      <p><b>アディチッタ（adhicitta）</b>とは禅定のことです。私たちは五官（眼耳鼻舌身）から入る情報だけに頼る次元の世界に閉じこめられています。人間に認識できるのは狭い範囲の情報で、思考も妄想も哲学も科学もすべてそれらの情報によって頭で組み立てたものに過ぎません。仏教では、その次元を超越した高いレベルの智慧が生じることを目指します。
      </p>


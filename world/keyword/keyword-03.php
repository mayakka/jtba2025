        </li>
        <li class="q-wabun">平安な心と忍耐は最高の修行です。<br>涅槃こそ最高だと諸々の仏たちは説きます。</li>
      </ul>

      <p><b>カンティー（Khantī）</b>には忍耐という意味もありますが、「平安」という意味もあります。つまり、「落ち着いていること」です。いいことがあったからといって舞い上がることもなく、つらいことがあってもくよくよ気に病むこともなく、冷静に落ち着いていること。これはすごく気をつけて実行しなければならないことです。心はいいことがあったら、喜んで舞い上がってしまいます。悪いことがあったらすぐに落ち込んでしまいます。どんなことがあっても混乱せずに、静かな心の状態を守ることはなかなかできないことです。
      </p>
      <p><b>ティティッカー（titikkhā）</b>というのは「忍耐」です。人はさまざまな苦しみを味わいます。どうしても人生の中ではいいことよりは悪いことの方が多いのです。いくら悪いことに遭遇しても忍耐を持つことがティティッカーです。苦しいことは、予告なしに突然やってきます。自分は何も悪いことなどしていないのに、外からさまざまな災難が襲ってきます。すぐに「なぜ自分がこういう目にあわなければいけないのか」などと不平不満の心が膨らみます。そこで忍耐することが、ティッティッカーです。
      </p>
      <p>お釈迦さまは「その二つが最高の修行ですよ」とおっしゃっています。一般的な修行のイメージとは違うかもしれませんが、最高の修行というのはこういうことなのです。何日間も断食をしたり冬に滝に打たれる行などをして、わざと苦しんで忍耐の精神を育てなくてもかまいません。ふつうの生活の中で、いくらでも、平安な心を作らなくてはならない場面と忍耐をしなければならない場面が起こってきます。
      </p>
      <p>これは「修行とは何か」という問いに対する仏教の答です。仏教では信仰とか祈りなど、宗教的なこととはまったく違うことを言います。『どんなものにも波立たない落ち着いた心』と『つらいことに対する忍耐をつくること』。いつの時代でも、どこにおいてでも、人は修行といえばこの二つだけやればいいのです。もしも修行がしたければ、まあこの二つだけやってみてください、とお釈迦さまはおっしゃっているのです。これはやってみるとかなりたいへんで、難しいのです。それに比べて、祈ったり、踊ったり、呪文を唱えたりすることはずいぶん簡単だと思います。けれどもそういうことは結局ただのごまかしにすぎないのではないでしょうか。
      </p>
      <p><b>カンティー</b>と<b>ティティッカー</b>という、二つの実践によって得られる心の最高の状態が「涅槃」なのです。涅槃とは、すべての現象から心が自由な状態で、現象に触れても心が少しも波打たないことです。一切の現象から離れている心。その涅槃こそ、人間の到達するべき最高の境地ですよ、と仏たちはおっしゃっています。この二つの修行をがんばっていると、最高の境地である涅槃にまで何ということもなく行けるのです。
      </p>
      <p>心の平安と忍耐こそ何よりも大切な人間の道だということ。これは誰にも反対できません。他の宗教を信じている人でも、宗教などまったく信じていない人でも、「これは間違いですよ」とは言えません。そういうところはお釈迦さまの言葉の本当に不思議なところです。簡単な言葉で、どんな人間にも否定できないことをおっしゃるのです。どんな時代のどんな人にも当てはまること。真理の言葉というのはこういう言葉なのです。</p>
    </section>


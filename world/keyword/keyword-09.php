        修行をしていてもこの惛沈が出てくると、無知が生まれてきてぼんやりとし、やる気がなくなってしまいます。本来、ヴィパッサナー瞑想をしていてエネルギーが減るはずはないのです。正しく不貪不瞋不痴で瞑想実践をしているならば、どんどん元気になってやる気が出てこないとおかしいのです。だから疲れてやる気がなくなったり眠くなったりするということは、貪瞋痴で瞑想実践をしてしまっているということになります。<br>

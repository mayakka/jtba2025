        怒りは自分のわがままから生じます。自分が気に入るか気に入らないか、という自己中心性がなければ怒りは生まれません。自分が気に入っている対象にはローバ（欲）が生じ、気に入らない対象にはドーサ（怒り）が生じます。一見、ローバ（欲）は明るくてドーサ（怒り）は暗いようですが、本当はどちらも暗い心です。明るいのは善心所だけで、不善心所はすべて暗い心なのです。<br>
        怒ると気分が悪くなるから心が怒りを好まないかというと、決してそういうことはありません。心は刺激を求めています。欲も怒りも、結局は刺激なのです。</p>

      <p>自分が悪いことをした、恥ずかしいことをした、と自分に対して怒ること
        仏教では、後悔をすることはとてもよくないことだとされています。後悔をすると心が暗くなって、心が冴えていかないからです。元気でがんばっている人でも、後悔がちょっと入ったら、すぐに心がしぼんでしまいます。後悔は大変危険で強いマイナスのエネルギーなのです。<br>
        自分がしてしまった悪行為を思い出すことは、悪行為を重ねることと同じことです。「あのときは悪いことをした」と思い出すと、その度に罪がどんどん重く大きくなるのです。思い出す度に印象を心に強くして、新しい印象を何回も作ってしまいます。
        後悔をする癖のある人は、歩いていても、料理をしていても、しょっちゅう後悔をしています。失敗してもそれを認めて前向きにがんばる人、自分の失敗を堂々と明るく言える人は、後悔はしません。失敗から何かを習っていい勉強になった、という感じで、明るく生きるのがいいのです。「どうしてあんなことをしてしまったのか」と悩むことは後悔であって、へたな行為（アクサラ）であり、成長して進んでいけなくなる生き方です。</p>

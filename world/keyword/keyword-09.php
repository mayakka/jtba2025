        貪（ローバ）は巨大な心所です。生命は自動的に自分が好むことをしようと、欲で行動するからです。食べたいから食べる。見たいから見る。聞きたいから聞く。考えたいから考える。妄想したいから妄想する。寝たいから寝る。それらすべての行為に貪という心所がはたらいています。ですから心所の中で私たちにいちばんなじんでいる不善心所は貪なのです。私たちにとってローバはあまりにも普通の心所であり、普通であるだけにとても捨てにくい不善心所です。「おかげさまで欲のない生活をしております」などと言う人でも、真に不貪の生活をしている人はめったにいません。<br>

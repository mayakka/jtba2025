        優れた人を見て「優れた人だ」と思うことはイッサー（嫉妬）ではありません。その人に対して怒りの心が生じるのが嫉妬です。もし嫉妬の心が生まれたら、「これは嫉妬だ」と明確に知っておくことが大切です。嫉妬は自分にないものに対して生じる怒りです。</p>

        嫉妬（イッサー）と慳（マッチャリヤ）は、外から見るとどちらなのかわからないときもあります。たとえば、誰かが「あの人はすばらしい人ですね」とほめるのを聞いて、おもしろくない暗い心が生じたとします。他人の優れたところをねたんでいれば嫉妬で、自分の優れたところにケチをつけられたように感じているならば慳です。自分の心を細やかに見て、これは嫉妬だ、これは慳だ、と確認します。人は自分の悪いところを隠そう隠そうとします。修行をする人は、正直に自分を明確に見ていくことが大切です。</p>

  <div class="keyword-honmon">

    <p>慈悲」は「私たちはどのように生きるべきか」という問いに対する仏教の答えです。<br>
    「私も他の生命も皆、幸福になりたい。互いに仲良くしなければ幸せに生きることはできない」…この生きる基本を忘れてはならないのです。<br>
    仏道が目指しているのは智慧によって得られる最終的な心の平安（解脱）です。しかし人は、悟りを得るまでも生きていかなければなりません。また、悟りを開いても、自分の寿命が終わるまでは生活しなければならないのです。<br>
    では、実際の生活の中でどのような生き方をするべきなのか。仏教では、そこで、慈悲の生き方を説くのです。<br>
    慈悲と智慧は仏教の二本柱と言えるほど、どちらも欠かせないものです。</p>

    <p>命というのはネットワークです。毒蛇もゴキブリも、ネットワークの一員です。いろんな形でお互いを支え合っているのです。独りで生きていける生命はいません。我々は、多くの生命に支えられています。いわば皆のお陰で生きているのです。<br>
    ですから互いに慈しみを実行することは絶対的なことで、呼吸をするのと同じように基本的なことです。慈悲を呼吸にたとえたのは、食べることよりさらに基本的で欠かせないものだからです。だからこそ、誰もがいろんな形で「愛」を語るのです。「愛こそ最高だ」と、よく聞くでしょう。では「慈悲」と「愛」はどのように違うのでしょうか。</p>

    <p>まず、「愛」という言葉は人間同士の関わりについてのみ語られています。<br>
    「慈悲」は人間だけではなくすべての生命との関わりです。仏教のsattā（サッター：衆生）という言葉には、我々に認知可能なすべての生命はもちろんのこと、神々や地獄の生命など他次元世界の生命もすべて入ります。慈悲はそれら無量の命に対する限りなく大きな優しさです。ですから「慈悲」はパーリ語でappamaññā-citta（無量心）といいます。</p>

    <p>次に、「愛」という言葉は定義が曖昧です。キリスト教には「神こそが愛なり」という定義があるようですが、具体的には今ひとつよくわからないのです。世間の「愛」にいたっては、その定義はまったくない状態です。家族への気持ちも、人を助けることも恋愛も、不倫関係も、すべて「愛」という一言でくくられています。人々は各自の個人的な価値観で「愛」を捉え、「愛」を実行しようとします。元々頼りにならない「個人的な価値観」というものは、気持ちや環境に左右されてドンドン変化します。同じ人でも朝と昼と夜で全然違う…それどころか、分単位、秒単位で気持ちは変化するのです。だから「真の優しさ、真の愛」とは何なのか、誰もよくわからないのです。<br>
    「あなたのために」と自分の価値観を押しつけて、人に迷惑をかけるのはよくあることです。</p>

    <p>理解が曖昧な概念は、生きる指針にはなりません。<br>
    仏教の特色は、何でも明確に微細にものごとを分析して定義することです。大ざっぱに考えようとはしないのです。その辺は真剣です。大ざっぱに「人を愛するのはすばらしい」と言うのは危険なのです。<br>
    「人を愛する」というのはどういう意味か、何をすることなのか、明確に知る必要があります。<br>
    世の中には「曖昧で抽象的な方がすばらしい」という文学的な考え方もありますが、お釈迦さまは、その立場はとりません。慈悲の概念も明確に分析されています。白黒をはっきりさせてこそ、真の清らかさに近づけるのです。白と黒の違いは「その心で生きると幸福になるか、不幸になるか」というポイントで分けられます。厳密な分析というのは、簡単なことではありません。ブッダの智慧でなければ、とてもできるものではないのです。<br>
    釈尊は、生命の生命に対する心のはたらきの中から純粋に幸福になるはたらきを四つ選ばれました。<br>
    それはmettā、karuṇā、muditā、upekkhā の四つで、それぞれ、慈・悲・喜・捨と訳されています。慈悲については具体的にしっかり理解して納得することが必要です。仏教では、四つの慈悲心の意味だけではなく、その育て方も丁寧に説かれています。<br>
    これから、慈・悲・喜・捨という四つの慈悲心についてそれぞれ述べたいと思います。


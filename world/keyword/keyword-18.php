      <p>Mettā は、「友情」という意味で日常的に使われていた言葉です。愛情は執着や嫉妬に姿を変えて人を苦しめることがありますが、友情はどんな時にもホッとできる明るく楽しい気持ちです。何でも話せて信頼できる親友と一緒にいる時の、気楽で楽しい気持ちを思い出してみてください。<br>
      どんな生命といても親友と一緒にいるような感じでいられれば、どれほど幸せなことでしょうか。Mettā を育てた人は、そういうすばらしい幸せを得られるのです。</p>

      <p>Mettā を育てる実践方法があります。
      「私は幸せでありますように、私の親しい生命は幸せでありますように、生きとし生けるものは幸せでありますように」という言葉を念じて、mettā で心を満たすのです。<br>
      「友情を育てられますように」と念じても効果はないのです。「幸せでありますように」と正直に思う時、そこにあるのが友情なのです。<br>
      心を優しい友情の気持ちでいっぱいにすると、慈しみの心が育つのです。</p>

      <p>「『私は幸せでありますように』というのは欲ではないですか」と言う人がいます。しかし、「幸せでいたい」と思うのは自然で正直な気持ちでしょう？「私は幸せではありませんように」と願う生命はいないのです。<br>
      心を育てるためには正直である必要があります。「私はどうなってもいいから他の人のために…」というのは無理があるのです。偽善的になると、慈しみから離れてしまいます。<br>
      「私だけが幸せであるように」と考えるのは間違っていますが、「私も、皆も幸せであるように」と考えるのは正しいのです。</p>

      <p>「私は幸せでありますように」と念じたら、次に、「私の親しい生命が幸せでありますように」と念じます。<br>
      この時は、まず仲の良い同性の友人を瞑想の対象に選びます。異性の友人は選ばないようにします。<br>
      家族に瞑想する時は、自分の奥さんや旦那さんや子供たちを真っ先に思い浮かべるのではなく、まず両親、お爺さん、お婆さん、兄弟、親戚などを選びます。これは、異性の友人や身内のことを思うと、mettā ではなく別の情が出てしまいがちだからです。自分が同性の親友といて楽しかったことを思い出してみてください。その幸福な気持ちがmettā です（親友がいない人は、幼い頃にお母さんと遊んで楽しかったことを思い出してもいいのです）。<br>
      自分の旦那さんや奥さんにmettā の瞑想をしたければ、mettā の気持ちが十分わかったところで、同じような気持ちを念じるようにすればいいでしょう。
        また、亡くなった人に対しては、慈悲の瞑想はしません。<br>
      親しい人の死はどうしても悲しみとつながるのです。悲しみも怒りの一種ですので、明るく幸福な慈しみの心を育てる瞑想にはふさわしくないのです。<br>
      慈悲の瞑想は、幸福になるための厳密な治療法なのです。ちょっと間違うと、清らかな慈しみを育てようとして、真っ黒い感情（欲や怒り）を育てる可能性があります。<br>
      瞑想のやり方は、心のはたらきについての鋭い智慧で説かれていることなので、決まったやり方を守った方が安全だと思います。<br>
      mettā の理解のために瞑想対象について細かく述べましたが、実際に実践する時は、「あの人」「この人」と対象を選ばずに「私の親しい人々」と全体的にまとめて瞑想するのが簡単でよい方法でしょう。</p>

      <p>それから「生きとし生けるものが幸せでありますように」と、生命全体に慈しみを広げて瞑想します。<br>
      これこそ慈悲の瞑想の代表と言える最勝の言葉です。朝起きた時、夜寝るとき、電車に乗っている時など、いつでも時間を見つけて「生きとし生けるものが幸せでありますように」と心の中で何度も念じるようにしてください。素晴らしい結果が得られます。この言葉こそ人を確実に幸福にする強力な呪文だと言えるのです。</p>

      <p>次に、「私の嫌いな生命が幸せでありますように、私を嫌っている生命が幸せでありますように」と、自分が嫌いな生命に対しても瞑想します。<br>
      これは、はじめは抵抗がある人もいるかもしれません。しかしこれを正直に念じられるようになった時には、本当の慈悲心が生まれているのです。<br>
      「好きな相手に優しくするのは当たり前。犬や猫でさえ好きな相手には優しい。私は『自分を邪魔する生命、嫌いな生命も幸せであるように』と思えるような人間になるんだ」と、立派な人格を目指すのです。</p>

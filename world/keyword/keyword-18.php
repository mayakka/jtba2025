    <section id="sect-2">
      <div class="key-term-sup">
        <h2 class="key-term">カルナー；悲</h2>
        <p class="pali-large"><span class="num-g">２．</span>Karuṇā</p>
        <p class="key-sup">他者の苦しみをなくしてあげたいという優しさ</p>
      </div>
      <p>自分の子供が病気になったり事故にあったとしたら、母親はどういう気持ちになるでしょうか。すごく純粋に、早くその子を助けてあげたいと思うでしょう。それがkaruṇā の気持ちです。子供がケガをしたら、母親は落ち込んでなどいません。急いで抱っこして、病院まで走って行くのです。病院が閉まっていてもめげません。かなり活発的です。そのように、心から人を助けようとする人は、とても明るく元気で活発な状態になるのです。相手を助けてあげて「ああ疲れた」とマイナスには思いません。「よかったよかった」と、自分が何かできたことを喜ぶのです。そういう優しい気持ちを育てて、不幸になるはずがありません。</p>

      <p>karuṇā は、「かわいそう…」と感情的になることではありません。感情的になると理性がなくなって、智慧が現れなくなるのです。たとえば「家族を放っておくとかわいそう」など感情的な理由で修行をやめたりすることは、理性的ではありません。慈悲の人は自己犠牲的な生き方はしません。お互いに良い道をさがします。理性的に自他のために活発に行動し、巧みに生きる人が慈悲の人です。感情は悩み苦しみの原因です。仏教では、感情を慈悲喜捨の心に入れ替えることを奨めます。慈悲は、智慧が発達するためにも必要で、自我をなくす方向にはたらきます。</p>

      <p>karuṇā の慈悲心を育てる実践法があります。「私の悩み苦しみがなくなりますように、私の親しい生命の悩み苦しみがなくなりますように、生きとし生けるものの悩み苦しみがなくなりますように」と念じて、karuṇā の気持ちで心を満たすのです。karuṇā は、人を助けてあげることが好きな人に育てやすい心です。また、ものごとを後回しにする怠け者には良い薬になります。明るく行動的になりたければ、karuṇā を育てるといいのです。karuṇā を実践すると活発になる上に、楽しさと充実感を感じて生きていくことができます。</p>

      <p>次に、「私の嫌いな生命の悩み苦しみがなくなりますように、私を嫌っている生命の悩み苦しみがなくなりますように」ということも念じます。これは、実生活の中でも、できるだけ実践してみるといいのです。たとえば、こちらは何もしていないのになぜか意地悪をする人がいたとします。そういうことをする人に対して腹を立てて憎むのではなく、「この人の悩み苦しみがなくなりますように、この人も幸せでありますように」と念じて、できれば助けてあげるのです。嫌な人に対して慈悲の心を育てると、強い慈悲心が生まれるのです。また、その時は見返りを期待する気持ちはないのだから、純粋な慈悲を育てることができます。それは嫌な人の仲間になるということではありません。自分を攻撃する人に対して怒らず、逆に心配してあげるような、優しい心を育てることなのです。「無知な愚か者に対して攻撃するほど愚かな者はいない」という釈尊の言葉があります。「怒る人に対して怒る人は、より悪い」そういう考え方でなければ、この世の中は幸福になりません。相手は愚か者だから攻撃するのです。自分が幸せになりたいのに、正しい方法がわからないのです。だから、憎むのではなく、心配してあげるのです。</p>

      <p>そのように、仏教では、自分の敵に対して憐れみを実践することを説きます。「自分に害を与えた嫌な人を助けるなど、そんなことができるわけがない」と言う人がいますが、試して悪いことではありません。どんな悪人でも生命なのだから、幸福でいて何が悪いのでしょうか。賢者はそのように考えるので、「敵」は誰もいないのです。</p>

      <p>ジャータカ物語（釈尊の前世物語）には、菩薩が自分をひどい目に遭わせた相手を助ける話がいくつもあります。自分が優しくしてあげた相手に殺されて死んでしまう話さえあります。ジャータカ物語の中の菩薩は、仏教徒の生き方のモデルです。大切なのは善い生き方をすることです。それによってたとえ殺されたとしても、別にたいしたことではないのです。人はいづれ死ぬのです。憐れみの心で悪人を助けてあげて死ぬのであれば、その死は無駄ではありません。それどころか、それこそ、真に立派な人にしかできないすばらしい行為ではないでしょうか。</p>
    </section>

    <section id="sect-3">
      <div class="key-term-sup">
        <h2 class="key-term">ムディター；喜</h2>
        <p class="pali-large"><span class="num-g">３．</span>Muditā</p>
        <p class="key-sup">他者の幸福を共に喜ぶ優しさ</p>
      </div>
      <p>慈悲には慈・悲・喜・捨という四種があります。<br>
        前回までに慈と悲のことを述べました。今回は「喜：muditā」です。これは他生命の幸せに「よかったなぁ」と共感できる気持ちです。<br>
        我が子が大学に合格したら、母親はすごく楽しいでしょう。その純粋なうれしさがmuditāです。<br>
        ふつうは、誰かが成功すると嫉妬します。先程の母親も、隣の子がもっと良い大学に入ったと聞いたとたんに悔しくなって、幸福がしぼんでしまうのです。誰かが成功すると関係ない他者が不幸になる…世の中ではそういうおかしなことがよく起こります。<br>
        muditā の人は、隣の子の合格を喜びます。がんばったのは他人なのに自分も幸福になると、自分はまったく苦労せず、いつでも幸福に生きられるのです。</p>

        <p>muditā の人は、常に他人の明るい側面を見てそれを喜ぶので、災難に遭っても幸福は消えません。交通事故に遭って「なんてついてないのか」とクヨクヨするのは不幸な人の思考です。muditā の人は「まあ命が助かってよかった」と明るくしています。いつも暗くて笑えない人は、muditā を育てるといいのです。</p>

        <p>muditā を育てる瞑想は、まず「私の願い事が叶えられますように」と念じ、「私の親しい生命、生きとし生けるもの、私が嫌いな生命、私を嫌っている生命」の願い事も叶えられますようにと次々と念じます。</p>

        <p>「嫌な人の願い事が叶うようになんてとんでもない」と言う人がいます。しかし、ここで「願い事」というのは、世俗的な願い事とは違うのです。人は「お金持ちになりたい、地位や名誉がほしい」などいろいろと願うでしょう。しかしそれは単なる妄想です。頭の中で夢を見ているのです。「歳を取りたくない、病気になりたくない、死にたくない」というのも、妄想です。具体的な願い事ではありません。また、そんなことが叶うはずもないのです。</p>

        <p>徹底的に具体的に人生を見てみましょう。自分の人生の長さは決まっています。人生は一秒一秒確実に縮んでいくのです。その一秒が実際の世界です。「大金持ちになりたい」など、そんなことは別に実現できなくても大したことはないでしょう。そんな妄想とは関係なく、生きるのに必要な願い事が一秒毎にあるのです。お腹がすいたら何か食べたい。駅に向かったら駅にたどり着きたい。もっと無意識的には、息を吸ったら吐きたい、吐いたら吸いたいということはいつでもあります。生きるというのは、大したことはないのです。常に「何かやりたい」という衝動があって、それをやることが生きることなのです。我々には秒単位で願い事があり、それをなんとかしようと次の秒でがんばるのです。呼吸することも、ご飯を食べることも、眠ることも、秒単位で成り立っているのです。それに失敗するとたいへんなのです。</p>

        <p>それが本当の願いの流れです。そういう願いは、どの生命にも、ずっと流れているのです。とにかく我々は、具体的な一秒単位で生きています。その一秒だけを成功すればいいのです。人生に成功する必要はありません。「人生」というのも、ただの観念的な言葉です。具体的には、今の一秒。それは自分で握ることができます。</p>

        <p>「私の嫌いな人」というのも妄想思考です。ただの人間関係の「言葉」にすぎません。自分と関係ある人々の中に、自分が好まない態度を取る人がいるということです。その人も、おだやかでストレスがなければ攻撃しないのです。だから嫌な人もスムーズに楽しく生きるならば、それはその人にとってだけではなく、自分にとっても良いことなのです。</p>

        <p>結局「願い事が叶えられますように」というのは「皆おだやかで、落ち着いて、ストレスなく生きていってほしい」という気持ちです。そういう気持ちで生きるとおだやかな喜びの生き方ができます。それこそが力強い生き方なのです。慈悲は真理の言葉なので、世俗の概念で捉えると間違います。ブッダは真理の目でご覧になり、人々が真に幸福になる道を説いておられるのです。</p>
    </section>

    <section id="sect-4">
      <div class="key-term-sup">
        <h2 class="key-term">ウペッカー：捨</h2>
        <p class="pali-large"><span class="num-g">４．</span>Upekkhā</p>
        <p class="key-sup">差別のない広々した平等な心でいる優しさ</p>
      </div>
      <p>今回のテーマは、慈・悲・喜・捨という四種の慈悲心の最後、捨 upekkhā 「平等心」です。Upa という接頭語には「落ち着いて、包括的に」という意味があり、それに ikkhati「見る、観察する」が合わさった動詞 upekkhati の名詞形がupekkhā で、「落ち着いて客観的に観察すること」というような意味になります。一方的に偏らず、偏見がない心です。その意味を見てもわかるように、upekkhā と智慧はかなり近いのです。「Upekkhā が育つ」と「智慧が育つ」は同義語と言っていいほどです。智慧を育てるためには、見たもの、聞いたものにとらわれず、固定概念を捨てることが必要です。 Upekkhā の「捨」という訳を見ると、一見「？」と思うのですが、「取らない、執着しない」というところからきていると思えば理解できます。</p>

      <p>世間で「人間は平等だ」と言う時は、「他の生命は平等ではない」という気持ちがあります。「平等を壊す人間」を攻撃するのが「平和運動」なのです。そういう「平等」はただのインチキです。</p>

      <p>仏教の「平等」は広大な概念です。sattā（生命）とは、宇宙全体の生命、多次元生命（神々・霊・餓鬼・鬼など）を含むのです。Upekkhā の人は、主観的な自分主義が消えた広い心で「生命は、いろんな形を取ってそれぞれ生きている。でも皆、同じ生命だ。私もただその一員にすぎない」と見るのです。</p>

      <p>仏教には輪廻という概念があります。我々は無始なる過去から、死んでは生まれ、死んでは生まれ、無限とも言える時間を生きているのです。その時間は、何千億年、何兆年などという小さなものではありません。一つの宇宙が消滅する時間単位でさえ、あっという間なのです。それほどの長い輪廻の中で、我々は皆、動物や虫や魚にも、天界や地獄にも、輪廻を繰り返してきました。だから誰が偉くて誰が偉くないなど、どうやって言えるのでしょうか。たまたま地位が高いなど、どうということはありません。来世では虫かもしれないのです。そこを理解すると、平等ということは当然のことになり、具体的に、厳密に、生命は平等だと見ることができるのです。</p>

      <p>Upekkhā を育てる慈悲の瞑想の言葉は「悟りの光が現れますように」です。この言葉を、自分、親しい生命、生きとし生けるもの、私が嫌いな生命、私を嫌っている生命に、次々と念じます。これは、心の汚れを消し去り、心の小さいレベルを破って無量の大きさの広々した心の状態をつくる修行です。それを理解して、リラックスしながらも真剣に念じてみてください。</p>

      <p>これまでに四つの慈悲心を説明しました。慈・悲・喜・捨は、それぞれ違う心のはたらきですが、四つとも生命に対する心の優しさです。生命との関係をどう持つべきかというと、そのどれかの心で接すればいいということです。その場その場で状況に応じて、そのどれかを実践するといいのです。Upekkhā（平等心）と mettā（友情）はちょっと似ています。しかし、平等心は、友情よりも、すごく広いのです。例えば、ある乱暴者が騒いでいるとします。 Mettā の慈しみがあると、「うるさいなあ」と気楽に言えます。乱暴者も優しい友人の言葉には怒りません。 Upekkhā の人は、静かに、「皆それぞれ自分の生き方をしているのだから、まあいいじゃないか」という感じで、なんということもなく落ち着いています。これは無関心とはまったく逆です。無関心は無知で、落ち着きは智慧です。落ち着きはものごとを理解することによって生まれます。誰に対しても静かに落ち着いている人がいたならば、周りの人々もとても気持ちがいいのです。</p>

      <p>四つの慈悲心は、それぞれ、無制限に大きく育てることができます。慈悲を育てるには、慈悲の瞑想が一番強力な方法です。四つの中から一つを選んで念じてもいいのですが、四つとも瞑想すると自分にあった慈悲心が自然に育ちます。</p>

      <p>慈悲が育つと、人は、信じられないほど変わります。はっきりと別人になるのです。すごく穏やかで、常に喜びの波で満たされた人になります。慈悲こそ、宗教を超えた真の幸福の道です。</p>

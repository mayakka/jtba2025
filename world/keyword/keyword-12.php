    <section>
      <div id="sect-2" class="key-term-sup">
        <h2 class="key-term">ヴェーダナー：受</h2>
        <p class="pali-large"><span class="num-g">２．</span>vedanā</p>
        <p class="key-sup">六処（眼耳鼻舌身意）で触れた情報を感じるはたらき</p>
      </div>
        <p>仏教では「感じること」を心の機能として、心所のひとつに数えています。ヴェーダナー（受）は「感じた」と意識化される以前の根本的で基本的なはたらきです。</p>

        <p>何かを知る前にそれを感じないと知ることはできません。光が目に入って、それを感じたら見える。感じなければ見えません。私たちの耳には様々な音が入ってきますが、それらの音に対してヴェーダナーという感じる機能がはたらいた音だけを認識するのです。何も感じなければ存在しないのと同じことで、我々には関係ありません。誰かにひどいことを言われたとしても、その声（音）を耳が感じなければ心に怒りは生まれません。その声（音）を感じて心が強烈に受け取ると、強烈な怒りが生じるのです。</p>

        <p>ですからこの受（ヴェーダナー）という心所は、人間の煩悩の大本となる非常に大事な心所なのです。人間のすべての迷いはこの受からはじまります。誰に何を言われても、心がそれを放っておくことができれば、悩みは消えてしまいます。楽しいときでも、客観的に観察すると、ある概念によって楽しみを感じていることがわかります。もしもそのことに執着がない場合は、別に楽しくもないのです。それがわかると「なるほど、受に基づいて渇愛があって、渇愛から苦が生まれるんだ」という智恵が生じます。</p>

        <p>受はたいへん重要な心所で、仏教哲学の心臓だとさえ言うことができるのです。なぜそれほどまでに重要かというと、「私」「魂」「真我」などという強烈な誤解が、このヴェーダナーから生まれてくるからです。</p>

        <p>何かに触れてそれを感じることは自然法則で、どうにもならないことです。ところがここで、生命は大きな間違いを犯してしまうのです。どういうことかというと、「私」という化け物が認識の中にサッと入り込んでしまうのです。たとえば目に情報が触れると、「光の波が目にぶつかって、目という感覚器官がそれを感じた」と正しく認識せずに、「私は見た」と思ってしまいます。それは瞬間のできごとなので、「私は見た」「私は聞いた」とはっきりと意識化されるわけではないのですが、「見た」「聞いた」と感じた瞬間に「私」という主語が入り込んでいるのです。</p>

        <p>yaṃ vedeti taṃ sañjānāti（ヤン　ヴェーデーティ　タン　サンジャーナーティ）「彼が感じたもの、それを彼は知る」というお釈迦様の言葉があります。ここで大切なことは、「彼」という主語が入っていることです。「彼」が知るのは「彼が感じたもの」のみであって、実際の外の世界を知っているわけではないのです。「彼が感じたもの」はそれぞれの主観ですから、一人一人みんな違います。ですから「みんなで一緒にこの絵を鑑賞しましょう」などということはあり得ないことです。</p>

        <p>自分の目で感じたものを知るのですから、「私が時計を見た」ということも成り立ちません。「私が目を開けていると、何かが目に触れて、ある感覚が生まれた。私はその感覚を認識した」と言った方がより正確です。実際は「私」というものもないのであり、「ある感覚からある認識が生じた」というのが、よりぴったりの言葉なのです。</p>

        <p>私たちは感じることによって、「苦」「楽」「不苦不楽」という三種の感じを受け取るとされています。しかし実際には、「苦」といってもさまざまな苦がありますし、感じる度に違う苦を感じます。それは「楽」も「不苦不楽」も同じことなので、結局は数え切れない無数の感覚があることになります。</p>

        <p>「私」、「私」と思っていたのは、ただ瞬間瞬間生滅していく無数のヴェーダナーであって、その無数のヴェーダナーから生じる束縛をなくすことができると、そこに心の安らぎがあります。それを体験することが悟りなのです。</p>
    </section>

    <section>
      <div id="sect-3" class="key-term-sup">
        <h2 class="key-term">サンニャー：想</h2>
        <p class="pali-large"><span class="num-g">３．</span>saññā</p>
        <p class="key-sup">六処（眼耳鼻舌身意）で触れた情報を区別する</p>
      </div>
        <p>対象を認識する場合、その対象を他のものと区別するために、心の中にはちょっとしたはたらきが生まれます。それがサンニャー（想）です。たとえば色形が目に触れると、目がそれを感じた瞬間に、言葉であれこれと考える以前に、心はサッと区別しています。</p>

        <p>区別するはたらきがないと認識はできません。認識するということは区別をするということです。生きているということは認識するということだから、生命にとってサンニャーはとても大事なはたらきです。</p>

        <p>とてもよく似たもの、たとえば同じ種類の鉛筆を二本見せられると、私たちはなかなか区別することができません。それでも「後で区別できるように、よく覚えておいてください」と言われたならば、どこかに違いはないか、なんとか区別できるところを探そうとします。それはサンニャーをはたらかせているのです。自分の頭に入れたサンニャーを使って区別するのです。</p>

        <p>私たちは、興味のある対象にサンニャーをつくります。たとえば誰かと話をしている時でも、耳には相手の話し声以外のたくさんの音も入ってきていますが、そういう関係ない音は無視しています。つまりサンニャーが生まれていないのですね。サンニャーが生まれない音は聞こえません。</p>

        <p>サンニャーは訓練によって鋭く強くすることもできます。勉強ができる人は、いろいろ工夫してサンニャーのはたらきを力強くしているのです。学校で授業を受けるだけでなく、自分でもそれについて考えて疑問や反論をつくったりすると、サンニャーがたくさん生まれてしっかり理解できます。</p>

        <p>私たちの心はいつでも新しいサンニャーををつくり続けています。誰かを見るときでも、見るたびに新しい人を見ているはずなのです。感覚器官には、まったく同一のものが触れることは決してありません。<br>
        対象も感覚器官もどんどん変化して流れ続けているのです。同じ人を二度見るということはないし、同じ音を二回聞くこともないのです。すべて一回きりです。たとえば昨日誰かに言われたことを、百回以上も繰り返して思い出しているとしても、思い出す度に心に触れることは違っているのです。けれども似ているものを二度、三度と認識すると、「同じものだ」と錯覚するのです。「妄想概念」といわれる認識概念がはたらいて、勝手に概念をつくってしまうのです。</p>

        <p>概念、知識、記憶などはすべてサンニャーの塊です。私たちが「私がいる」「魂がある」などと錯覚する原因となっているいちばんの元凶はヴェーダナー（受）という感受作用ですが、このサンニャーもかなり錯覚の原因になっています。私たちは概念や知識、記憶などによって「私がいる」と思うのです。<br>
        たとえば『名前』という知識があります。自分が生まれてから今までずっと同じ名前だから、同じ人間だと思ってしまうのです。それも完全な妄想で、ただのサンニャーの塊に「自分」だとアイデンティティーをつくっているのです。サンニャーによって、無常が見えなくなってしまうのですね。赤ちゃんの時の自分と現在の自分を冷静に見ると、同じなのは名前だけで、身体も、考え方も、好き嫌いも、まったく違うのです。漠然と「変わらない何か」があるように錯覚しているだけです。</p>

        <p>瞬間瞬間、いろいろなサンニャーが生まれては消え生まれては消え、流れ続けていきます。そこには「何か変わらぬもの」「私」「魂」などはありません。</p>

        <p>ただ止まることなく生滅をくり返し、流れていくサンニャーという心のはたらきがあるだけです。</p>
    </section>

   <section>
      <div id="sect-4" class="key-term-sup">
        <h2 class="key-term">チェータナー：思</h2>
        <p class="pali-large"><span class="num-g">４．</span>cetanā</p>
        <p class="key-sup">行為の動機づけをするはたらき、意志</p>
      </div>
      <p>心には常に「心がひとつ回転したら、次の回転をせざるを得ない」というポテンシャルエネルギーのようなものが生まれています。たとえば手を上げると、そのまま上げ続けるか、止めるか、下げるか、何かをするのです。どれをするにしても、そこには「～しよう」という意志がはたらいています。人はいつでも何かをしたい。「きれいに止まりたい」という気持ちはありません。</p>

      <p>心所としてのチェータナーは基本的で根本的なわずかなエネルギーです。けれどもチェータナーはすべての心にはたらくのですから、巨大なエネルギーだと見た方がいいのです。ヴェーダナーやサンニャーも同じことで、私たちはずっと共一切心所のはたらきの中で生きているのだから、これらはかなり大きなエネルギーなのです。</p>

      <p>目を開けると自動的に何かが見えるのですが、その時でもやはり心が見たいから見ているのであってそこには何となく、チェータナー（意志）のはたらきがあります。意識して見ようとするときには、かなりのチェータナーがはたらいています。「立ちたい」と思うだけで立たないことがありますが、それは心にチェータナーが生まれたのだけれども、体を動かす程チェータナーが強くないのです。「立ちたい」と思ってすぐに立つ場合は、チェータナーのエネルギーが強いのです。</p>

      <p>そのように、行動を起こすか起こさないかということは、チェータナーが決めるのです。何かを考えているときは、それを考えたいから考えているのです。考えたくないことは考えません。考えるのをやめるときは、やめたいからやめるのです。どんな場合にも、チェータナーという心所がはたらいています。</p>

      <p>「カルマ（業）」というよく知られている言葉がありますが、そのカルマというのはチェータナーのことなのです。心は常に認識していますが、それも認識したいから認識しているのであって、そこに意志がはたらいています。カルマから逃げることはできないのです。大胆な行為だけがカルマで、普通の日常の行動はカルマではないということではありません。何かを見る度に、何かを考える度に、何かを思う度に、手を上げる度に、手を下げる度に、私たちはカルマを蓄積しているのであって、行動する意志がカルマなのです。たとえば本を読んでいるなら、「本を読みたい」という意志がずっとはたらいているから読んでいるのです。そこにカルマのはたらきがあります。</p>

      <p>チェータナ一には、カルマになるチェータナーとカルマにならないチェータナーがあります。いちばん基本的なチェータナーはカルマになりません。目を開けてたまたま何かが見えた場合は、別にカルマにはならないのです。何かを見ようとして見ると、そこにカルマが生まれています。生命が何かを認識した瞬間に、意志も生まれています。この意志がわずかでも強くなったらカルマになるのです。弱いチェータナーは行為を起こすだけで消えてしまいます。強いチェータナーは心にエネルギーとして蓄積されていくのです。その蓄積されたエネルギーがカルマになります。</p>

      <p>誰かにぶつかって転んだとします。「痛い」と思った瞬間にもチェータナーがはたらいて痛みを感じているのですが、それはカルマにはなりません。そこに欲や怒りの感情が入ったとたんにカルマになります。同じことでも、悪いカルマになる場合もあるし、善いカルマになる場合もあります。ぶつかった人に腹を立てて怒ったら、悪いカルマになります。<br>
      「ケガをしたではないか、弁償しろ」と怒鳴ったりすると、かなりの欲や怒りで、悪いカルマとなります。逆に相手のことを気遣って心配したならば、善いカルマになるのです。</p>

      <p> 自分の感情がコントロールできない場合は、「私はこうしたいと思っている（こういうチェータナーが今はたらいている）。これは、善いカルマをつくるだろうか、悪いカルマをつくるだろうか」と客観的に分析します。そうすれば、心を善い方向に向けることができます。善いチェータナーのエネルギーはどんどん育てて強くすることができます。<br>
      そのように、心所を理解するのは、心を育てるためなのです。心のはたらきを論理的に理解することによって自信が出て、やる気も出るのです。</p>
    </section>

    <section>
      <div id="sect-5" class="key-term-sup">
        <h2 class="key-term">エーカッガター：一境性</h2>
        <p class="pali-large"><span class="num-g">５．</span>ekaggatā</p>
        <p class="key-sup">認識対象に集中するはたらき</p>
      </div>
        <p>生命は瞬間、瞬間、常に何かを認識して生きていますが、認識するためにはその瞬間に認識対象につかまっている必要があります。一境性というのは、瞬間、瞬間、認識対象に集中するはたらきです。私たちは、見たり、聞いたり、味わったり、考えたりする対象に、認識する瞬間に集中しています。その瞬間の集中が一境性です。</p>

        <p>一境性は、集中力という以前の基本的なかすかなはたらきですが、この集中がなければ認識は生まれません。ですから一境性も、他の共一切心心所と同様とても大切な役割を果たしているといえます。心がいろいろなところに走り回っている状態を「集中力がない」と言いますが、それは、集中が一カ所に留まらないということで、一境性は常にはたらいています。一境性が一カ所に止まることを「集中力」と呼ぶのです。</p>

        <p>ものごとにはいろいろな無数の側面があります。一境性は、認識する瞬間に自分が認識したい側面に心所（心のはたらき）を統一させるのです。たとえばある人が「この絵の青い色の使い方が神秘的ですね」と言うと、皆が青色に気をつけて、絵をその側面で見ます。そうすると同じ絵を見ても、違うものが見えてくるのです。その瞬間に心が何に集中したかによって、違うものが見えるのです。子供が遊んでいても、その子が自分にとって良い存在だと思うとかわいいと思うし、自分にとって良い存在ではないと思うとうるさいと感じます。自分のダンナさんにしても、時々は面白い人になるし、時々はいない方がいい人になるし、時々は早く帰ってきてほしい人になるし、その時その時、自分が何かにしぼって相手を見ているのです。本当に相手を正しく見ている人はいません。</p>


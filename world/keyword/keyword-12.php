      <div id="sect-1" class="key-term-sup">
        <h2 class="key-term">パッサ：触</h2>
        <p class="pali-large"><span class="num-g">１．</span>phassa</p>
        <p class="key-sup">六処（眼耳鼻舌身意）にそれぞれの対象（色声香味触法）が触れること</p>
      </div>

        <p>パッサ（触）は、触れること、コンタクトをとることです。<br>
        それぞれの感覚器官に触れる対象は、それぞれ決まっています。たとえば、目は色形に触れます。外界全体にではなく、そのほんの一部である色形だけに触れるのです。見えるためにはいろいろな条件も必要です。対象に近づきすぎても、対象から遠すぎても見えません。一定量の光も必要ですし、光の種類や本人の視力によっても見え方はまったく違ってきます。人間の目は、紫外線や赤外線には触れることができません。<br>
        同様に、耳は音、鼻は香り、舌は味、身体は熱や堅さ、意（心）は法（概念）に、限られた条件の下で触れるのです。</p>

        <p>目で何かを見るときには、必ず心（viññana：ヴィンニャーナ：認識作用）がはたらいています。目と色形とviññana との三つが揃ったところに phassa という心所が生じるのです。「認識作用」は「生きていること」と言い換えることができます。これは単純なことで、生きている目を開けると何かが見える、ということです。心は瞬間瞬間、生滅変化しています。生命は心が生じているすべての瞬間に、必ず何かの対象とコンタクトをとっているのです。</p>

        <p>六つの器官が認識する情報は、それぞれまったく別々です。<br>
        たとえば色形と音はまったく違うものであって、お互いになんの関係もありません。また、それらの情報は固定していません。固定している固いものではなく、ずーっと流れつづけています。色形も光として流れてきて目にぶつかるのです。音も、波として流れてこないと聞こえません。味も、身体に触れるものも、同様です。「固定された固体が身体に触れるではないか」と思うのは錯覚で、触れるものはずーっと流れていなければ認識できないのです。私たちが「永遠不滅の魂」などを認識することは不可能です。</p>

        <p>六つのチャンネルに六種のデータが絶え間なく流れてくるのですが、五官（眼耳鼻舌身）は今ここにあるものを認識するだけなので、それほど問題はつく りません。さまざまな悩み苦しみをつくり出すのは六番目の意（心）なのです。心は、「あるもの」にも「ないもの」にも触れます。</p>

        <p>仏教では、私たちが 「ある」と思っているほとんどすべてのものは、実際にあるのではないと説いています。<br>
        たとえば。『人』というものは実在しません。『私』『あなた』『動物』『家』『今日』『昨日』『明日』などなど、それらはすべて実在しないのです。『今日』はどこに実在するのでしょうか。どこにもないのです。ですから心は「ないもの」のことを考えていることがほとんどです。私たちが、泣いたり、喧嘩したり、喜んだり、好きになったり嫌いになったりしているのは、心の中でつくっている幻想の世界です。</p>

        <p>私たちは多くのものを「ある」と勘違いして、怒りや欲でたくさんの感情をつくり出します。頭の中でつくった幻想を、「好きだ」「嫌いだ」とどんどん膨らませて、たいへん苦しんでいます。心はしょっちゅう「感情のやまびこ」に触れていると理解するとわかりやすいかもしれません。</p>

        <p>ヴィパッサナーの実践をすると、パッサがよく理解できるようになります。たとえば音が耳に入る。「音」を感じたらそこにパッサがあったのです。よく精神集中ができたならば、巨大なパッサが出てきて、コンタクトというのはいかにすごいかということがわかります。「触れる」ということから 認識が始まります。ですからヴィパッサナーでは、パッサをとても大切な心のはたらきとしているのです。</p>

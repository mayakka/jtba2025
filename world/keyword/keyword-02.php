    <p>テーラワーダ仏教はお釈迦さまの教えをできるだけ忠実に守ろうとするので、保守的だと言われることもあります。でもテーラワーダでは、お釈迦さまの教え以上の道はないことを、自分たちの体験から確信しています。お釈迦さまが説かれた道とは、簡単に言うと、四聖諦、因縁の教え、八聖道と、悟りを開いて解脱をするためのヴィパッサナー瞑想という修行方法です。お釈迦さまは、解脱をするために必要なことは心の執着をなくすことであり、煩悩を消すことであるとおっしゃいました。

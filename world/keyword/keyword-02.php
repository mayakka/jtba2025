    <p>テーラワーダの長老方は、お釈迦さまの教えからはずれないように、ただ一心にお釈迦さまの教えを守っていくことに自分の全責任をおいていらっしゃいます。お釈迦さまの教えに自分の解釈を加えたりすることは、いっさい拒否します。そしてパーリ経典に記されたお釈迦さまの道からはずれないように生きることを何よりも大切にします。お釈迦さまが亡くなられた当時でも、そういう長老方の態度が保守的だと批判する人々がいました。それらの人々は後にテーラワーダから離れ、大衆部と呼ばれる宗派をつくりました。その大衆部も、いくつもの分派ができ、お釈迦さまの入滅後200年くらい経つと18もの宗派に分裂してしまいました。これらはまとめて部派仏教と呼ばれています。</p>
    <p>お釈迦さまの入滅後500年ほど経つと、部派仏教を批判する新しい動きが現れました。そしてこちらこそ優れているという意味を込めて、自分たちのことを大乗仏教と称し、部派仏教のことを小乗仏教と呼びました。日本でテーラワーダ仏教を小乗仏教と呼ぶ人々がいますが、インドの大乗仏教が小乗仏教と呼んでいたのはテーラワーダではありません。実際に小乗と呼ばれていた部派仏教は、現在ではひとつも残っていません。テーラワーダは、大乗や小乗の元の根本的な教えだと理解した方がいいと思います。

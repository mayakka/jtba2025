    <p>また、私達の体内には無数の微生物が住んでいて、なにかしら有用な活動をしています。それらの生命に「これは私の体ですから、出て行ってください」と言って追い出してしまったら、「私」も死んでしまいます。ひとつの体の中にも、多くの生命体が入り込み、お互い助け合って共存しているのです。</p>
    <p>ですから、「私」を発見する以前に、生命に対する「やさしい心」というのは大変必要な条件になります。</p>

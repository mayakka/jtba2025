<section id="sect-2">
  <h2 class="headline-leaf">慈悲の瞑想の実践方法</h2>

  <div class="honmon-indent">
    <p>次に実践ですが、ここでは「すべての行動は心に基づく」という法則を用いています。人間が行動する前には、必ず先立って「こう行動しよう」という心の働きがあるのです。</p>

    <p>「慈しみ」を実践するのにもっとも簡単で早い方法は、心そのものを「慈しみの心」にしてしまうことです。</p>
    <p>道徳やきびしい戒律を守ったり、困っている人を助けたりといった行動をしていても、もしも心が清らかでなかったらそれほど意味がないし、あとで疲れたり、嫌になる可能性さえあります。でも心が「慈悲の心」になってさえいれば、そこから先の行動は、どんなことでも素晴らしいものに変わるのです。</p>

    <p>ですから、幸福になるためには、人生で成功するためには、平和で無事で争いがなく、堂々と美しく生きていくためには、ただ「やさしい心」さえ作ればいいのです。</p>

    <p>それには、人を見たら自分とみる、相手を「他人」と見ずに「自分」としてプロジェクション（投影）してみることです。それができた瞬間に、問題はハッキリ明確に見えてきます。例えば、何か問題にぶつかっている人を励ましたい場合は、もし自分が相手の立場だったら、どのようにしてもらったら元気が出て、問題を解決できる力が湧いてくるだろうか……というように、相手の立場を自己に投影してみるのです。</p>

    <p>それを上手くやるためには、やはり「慈悲」というものを育てなければなりません。「慈悲の心」を育てるためには、自分の心にず～っと反復して言い聞かせてやればよいのです。そうして「念じる」ことは、大変効果的なトレーニングになります。</p>

    <p>この「慈悲の瞑想」は、「ヴィパッサナー」とは違い、伝統的なごく普通の瞑想法ですから、ある意味では入っていきやすい実践だとも言えます。しかし、この慈悲を念じる実践は、決してヴィパッサナーの準備段階ということだけにとどまらず、すべての仏道修行において基礎になるもので、日常的にいつも念じているのが望ましいのです。</p>

    <p>まず最初に、自分自身に対して慈悲の心を作ります。どんな生命にとっても、いちばん大事で最優先されるのは自分であるというのは、ごく当たり前のことですから、まずそこのところは正直な心で「私は幸せでありますように」と念じます。「幸せ」とはなんですか？と考える必要はありません。幸せとは少なくとも良いことでしょう……というふうに大雑把に理解しておけばいいのです。</p>

    <p>次に「私の悩み苦しみがなくなりますように」「私の願いごとが叶えられますように」、そして私達はいちおう仏教の実践をしていますから「私に悟りの光が現れますように」と念じます。もし「悟り」という言葉が解かりにくければ「知恵が現れますように」でもかまいません。</p>

    <p>二番目には、さきほど出てきましたプロジェクションということでもありますが、自分の「親しい生命」、いちばん幸福になって欲しいと感じる生命がいますから、その生命（自分の子供や両親、友人、身近な生命、動物、鳥、魚、昆虫、その他‥）のことを同様に念じます。</p>

    <p>そして三番目に、やはりすべての生命が幸福であったほうがいちばんありがたいことだと思って、それから努力して「生きとし生けるもの」の幸福を念じます。</p>

    <p>この三段階で、あらかじめ決めておいた言葉を唱えて「念じ」ていきます。言葉は、静かに心にしみこんでいくように、丁寧に丹念に念じます。（何人かで一緒に実践するときは声を出して唱えることもありますが、一人で実践するときは、声を出さなくてかまいません）姿勢は背筋と頭をまっすぐにして、目を閉じてください。</p>

  </div>
</section>


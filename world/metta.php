    <p>簡単に言えば、この「私」という実感さえなければ、問題は何もないのです。しかし、これはなかなか消えるものではないのです。誰かが体に触ったら「私に触りました」と感じるし、冷たい空気が体に触れたら「私は寒い」というように、簡単に「私」という主語は出てきますが、しかしそのことは大問題なのです。「私」がいるから「他人」がいて、だから人間関係が悪くなったりする。「私」がいるから、他の人間と競争するためにいろんな才能を身につけなくてはならなくなる。例えばこのように、問題が生じてきます。</p>

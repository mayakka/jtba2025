<section id="sect-4"  class="raihai-box">
  <h2 class="headline-bb2c">懺悔誓願の文</h2>
  <div class="raihai-jp">
    <p>懺悔いたします。</p>
    <p>無明の闇におおわれて、身、口、意、の三業によっておかしてしまったあやまちがあります。</p>
    <p>仏、法、僧、に対するあやまち、恩師に対するあやまち、生きとし生けるものに対するあやまち。</p>
    <p>これ等いっさいのあやまちを懺悔いたします。</p>
    <p>また、自分が受けた他の人々のあやまちも許します。</p>
    <p>この様に雑事を離れ、独り静かに自己の心身を念をもって観つめるとき、瞬間、瞬間、変化生滅しつづける現象をヴィパッサナーによって洞察し、真の幸福を得て、解脱の道へ導かれますようにと、ここに誓願をいたします。</p>
  </div>

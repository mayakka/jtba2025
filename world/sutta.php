<section id="sect-1" class="sutta-box">
  <h2 class="headline-suttaindex"><a href="sutta-01">諸仏の教え <span class="small-eng">(Dhammapada Nos. 183-185)</span></a></h2>
  <p>仏教の真髄をまとめてお釈迦様が説かれた人間の生きるべき普遍的な教え。もっともポピュラーな初期仏教聖典のひとつ『ダンマパダ（法句経）』に収録された詩です。この句の意味をよく理解して毎日念ずることにより、我々のこころの怒りや、他を害する気持はなくなります。特に最初の一文（Dhammapada Nos.183）は、過去七仏がみなこの詩を教えたとされることから『七仏通誡偈』と呼ばれます。「諸悪莫作　衆善奉行　自浄其意　是諸仏教」の漢訳は、大乗仏教の影響の強い東アジアでも広く知られています。<br>
    パーリ語仏教用語集に掲載された<a href="<?php echo esc_url( home_url( '/world/keyword/keyword-03/' ) ); ?>">詳しい解説</a>もご参照ください。</p>
  <a href="sutta-01" class="button-sutta">経典を読む</a>
</section>

<section id="sect-2" class="sutta-box">
  <h2 class="headline-suttaindex"><a href="sutta-02">仏法僧（三宝）の徳の偈文</a></h2>
  <p>仏法僧（ブッダ・ダンマ・サンガ）の三宝の徳を数え上げ、それらが論理的にも科学的にもどのようにみても確かなものであると承知・確信をしたうえで、「私は帰依します」と表明する偈文です。「仏教は皆が信じているからとか、脅されたり、欲の願いで訳の分からないものを信仰する宗教ではない」という特質がよく表現された偈文です。</p>
  <a href="sutta-02" class="button-sutta">経典を読む</a>
</section>

<section id="sect-3" class="sutta-box">
  <h2 class="headline-suttaindex"><a href="sutta-03">三宝に帰依するための偈文 <span class="small-eng">Tisaraṇa Vandanā</span></a></h2>
  <p>仏法僧の三宝に帰依し、一切衆生の幸福を願い、身体への執着を戒め、「可愛い」自分もまた衆生と共に死すべき存在と観ずる……。仏法のエッセンスが凝縮された、日常生活において繰り返し唱えるのに適した偈文です。口語体の訳文と併せて、関西ダンマサークルの方々がスマナサーラ長老の指導のもと和賛調に訳された「日夜の想い」もご覧ください。</p>
  <a href="sutta-03" class="button-sutta">経典を読む</a>
</section>

<section id="sect-4" class="sutta-box">
  <h2 class="headline-suttaindex"><a href="sutta-04">因縁の教え <span class="small-eng">Paticca Samuppādo</span></a></h2>
  <p>『因縁の教え（順観・滅観）』は、無明・行・識・名色・六処・触・受・愛（渇愛）・取（固執）・有・生・老死の「十二縁起」を順逆に観察し、因果の道理を明らかにした経典。続いて収録された『釈迦牟尼仏陀が無上正覚を得たとき最初に述べられた歓喜の言葉（多くの生涯の偈）』は、『ダンマパダ（法句経）』に保存されたお釈迦様の力強い肉声です。</p>
  <a href="sutta-04" class="button-sutta">経典を読む</a>
</section>

<section id="sect-5" class="sutta-box">
  <h2 class="headline-suttaindex"><a href="sutta-05">宝経 <span class="small-eng">Ratana Suttaṃ</span></a></h2>
  <p>最古層の仏教聖典に属する『スッタニパータ』（中村元訳『ブッダのことば』で知られる）にも収められた経典（Nos. 222-238）。テーラワーダ仏教の世界では古くから重要な護経（paritta）のひとつとされ、幸福を願い危険から身を護るために唱えられました。仏法僧の限りない徳を、美しい比喩をつかって賞賛した経典です。</p>
  <a href="sutta-05" class="button-sutta">経典を読む</a>
</section>

<section id="sect-6" class="sutta-box">
  <h2 class="headline-suttaindex"><a href="sutta-06">慈経 <span class="small-eng">Metta Suttaṃ</span></a></h2>
  <p>宝経と同じく、『スッタニパータ』にも収められ（Nos. 143-152）、テーラワーダ仏教において特に重要視されてきた経典です。釈迦尊は「一切の生きとし生けるものは幸せであれ」と願う慈しみ（metta）の念を育て、しっかりと保つことの大切さを、抽象論ではなく具体的な生き方の指針として、懇切丁寧に説かれています。</p>
  <a href="sutta-06" class="button-sutta">経典を読む</a>

  <div class="honmon-indent">
    <p>歩く瞑想は、30分～１時間くらい時間を決めて集中的に実践する方法もありますし、日常生活の中で歩く度に実践するという方法もあります。どちらでも、自分の生活にあわせて実践すればよいのです。日常生活の中で歩くときは、「左足、右足、左足、右足…」と足の動きを感じて頭の中で言葉で確認しながらふつうに歩きます。<br>
      集中的に歩く瞑想を実践するときは、まず背筋を伸ばして、手を前か後ろで組みます。左足から歩くならば、左足に神経を集中させ、「左足」とこころの中で言葉で確認し、同じく「あげます」と実況しながら左足を上げ、「運びます」と実況しながら左足を運び、「おろします」と実況しながら左足を降ろす。次に、右足に神経を集中させて「右足」と確認し、「あげます」と実況しながら右足を上げ、「運びます」と実況しながら右足を運び、「おろします」と実況しながら右足を降ろす。このように実況中継を絶やすことなく歩くことを続けるのです。<br>
      歩く瞑想実践は、わざわざゆっくりと歩く必要はありません。足の動きを感じやすい、実況しやすいスピードで、自然に歩いてください。<br>
      ポイントは歩く際に、足の感覚を丁寧に感じることです。足をあげると同時に、あげるという動きを感じてみる。足を運ぶときは、運ぶという動きを感じてみる。足をおろすときは、おろすという動きを感じてみる。<br>
      はじめはうまくいかないかもしれませんが、とにかくチャレンジしてください。どうしてもできないと思うときも「左足、上げます、運びます、降ろします、右足、上げます、運びます、降ろします……」と隙間なく実況中継することだけでもつづけてみてください。徐々にできるようになります。<br>
      また、このシンプルさに飽きてきてつまらなくなって、頭はどこかへ逃げたくなるかもしれません。気にせずにつづけてみてください。<br>
      歩くということは無意味なことではなくて、人生の大事な一部ですから、シンプルだと軽視するわけにはいきません。私たちはいろいろ複雑なことをしていると思っていますが、生きる上で人々が行っているすべての行為を分析してみると、すべてがあきれるほどシンプルであることがわかります。それこそが、複雑な人生に対する答なのです。<br>
      初心者には歩く瞑想が入りやすいでしょう。ブッダの時代から、悟りを開いた人は、坐る瞑想より長い時間歩く実践をすることによって成長しました。歩く瞑想は、眠くならないし、早く落ち着くし、体と心の調和が速やかに実現できる実践方法なのです。

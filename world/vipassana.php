<section id="sect-6">
  <h2 class="headline-leaf">立つヴィパッサナー実践法</h2>
  <div class="honmon-indent">
    <p>いま座っているということにして、「立つ」実践の方法を説明してみましょう。<br>
      できるだけゆっくりと、手の動き、からだの動きを実況しながら、立ってください。立ち上がったら、「背中を伸ばします」と背筋を伸ばしてください。「手を動かします、手を組みます」などと実況しながら両手を前か後ろに軽く結んでください。目は半眼にしてください。意識を足の裏にしっかりと向けてください。<br>
      何も考えず、単純に、「立ってます、感じてます、立ってます、感じてます……」と足の裏に集中して、実況中継をつづけてください。立つ瞑想実践は10分間くらいで十分です。</p>
  </div>
</section>

<section  id="sect-7">
  <h2 class="headline-leaf">座るヴィパッサナー実践法</h2>
  <div class="honmon-indent">
    <p>座布団か椅子を用意します。<br>
      「坐ります」「しゃがみます」「手を動かします」などと動作を実況中継しながら、楽な形で座ります。足を組む場合は「足を組みます」と足を組み、「背筋を伸ばします」と、実況中継しながら背中をまっすぐにします。「目を閉じます」と目を閉じます。三回くらい「吸います、吐きます」と実況しながら深呼吸します。<br>
      次に「待ちます」と、自分の中に生じてくるものを待ちます。<br>
      それから、「膨らみ」「縮み」「痛み」「しびれ」など、気になるからだの感覚を、実況し続けます。<br>
      「雑念」「妄想」「眠気」「苛立ち」などの心の感覚も、実況します。どれかひとつのことに集中する必要はありません。ありのままの状態を実況中継します。<br>
      坐る瞑想は、自分のペースで、15分～45分間くらいすればいいでしょう。無理にがんばって長く座る必要はありません。瞑想を終了するときは、ひとつひとつの動作を丁寧に実況しながら「終わります」としっかり確認してから終えてください。</p>
  </div>
